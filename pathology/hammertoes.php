                  <h4>Cause:</h4> <p>Despite popular belief, curling of your toes is not due to poor choices of shoe gear. Rather, biomechanical factors are usually to blame. Biomechanics is the term used to describe how the foot and ankle move when you walk or stand. When we refer to someone as "flat footed" we are really describing is how biomechanics has affected their foot. Specifically, if the foot rolls in too much when you walk, we call that pronation. So a person who pronates has altered or abnormal biomechanics. When a person has normal biomechanics, there is an order motion to the muscles and joints of the feet and ankles. Altered, or abnormal biomechanics will lead to abnormal motion of the joints and muscles. Biomechanics then is used as a predictive model - telling us what happens to the foot in the presence of abnormal motions such as pronation.</p> <p>There are three distinct possible causes of hammertoes. Each is based on an abnormality in the biomechanics of the foot and ankle during the walking cycle.</p> <p style="text-align: center;"><img src="/images/pathology/toe.jpg" alt="Example of extensor and flexor tendons of the toe" /></p> <h4>Flexor Stabilization: </h4> <p>The flexors tendons are located on the bottom of the toe. These tendons should exert the same amount of force as their opposing tendons, the extensors tendons, which are located on the top of the toe. When the force generated by the contracture of the extensosr is equal to that of the flexors, the toe stays stablized during the walking cycle.</p> <p style="text-align: center;"><strong>Force of Extensors = Force of Flexors = Toe stays straight</strong></p> <p>In a person who is pronated or flat footed, the flexor tendons pull harder than the extensors, making the toe unstable during the walking cycle. Given enough time, this instability leads to a gradual curling of the toe. A hammertoe is born.</p> <p style="text-align: center;"><strong>Force of Flexors Force of Extensors = Unstable curled toe</strong></p> <h4>Extensor Substitution: </h4> <p>This condition is essentially the opposite of flexor stabilization. In this case, the extensor tendons over power the flexor tendons during the swing phase of the walking cycle(the foot swinging forward and off the ground). This leads to contracture of the toes. This phenomenon is commonly seen with people who have a very high arch.</p> <p style="text-align: center;"><strong>Force of Extensors  Force of Flexors = Curled Toe</strong></p> <h4>Flexor stabilization:</h4> <p>A weak calf muscle causes the flexor tendons of the foot to compensate, or carry more of the load. The flexor tendons overpower the extensors of the toe causing an unstable toe leading to curling.</p>
