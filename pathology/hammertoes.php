                  <h3>Treatment:</h3> <p>As we have seen, most causes of hammertoes are a result of biomechanical problems of the foot. And while no conservative treatment can straighten the toes once curled, Treatments aim at correcting the underlying biomechanical abnormality can sometimes keep the toes from getting worse. The most effective biomechanical therapy are custom orthotics. These "arch supports" help to reestablish the normal force generated at the toe by the flexor and extensor tendons.</p> <p style="text-align: center;"><strong>Orthotics + Force of Flexors = Orthotics + Force of Extensors</strong></p> <h4>Other non-surgical treatments include:</h4> <ul> <li>Wearing shoes with larger toe boxed, preventing rubbing of the toes on the shoes.</li> <li>Wearing non-medicated padding over the boney prominence.</li> <li>Sanding the calluses on a regular basis.</li> </ul> <h3>Surgical:</h3> <p>Surgical treatment consist of stabilizing the toe during the walking cycle. This is done a number of ways, depending the stiffness of the toe, presence of other deformities and overall health o f the patient.</p>
