        <h3>Treatment:</h3> 
        <p>In the early stages on Hallux Limitus/Rigidus, non-surgical care maybe of benefit. This includes <span itemprop="possibletreatment">custom arch supports to better align the first metatarsal relative to the proximal phalanx.</span> Orthopedic rockerbottom shoes for non-surgical candidates can also be of use. <span itemprop"possibletreatment">Cortisone injections into the joint can also temporarily alleviate the symptoms.</span> Generally, however, this condition requires surgery. Surgical management falls into two broadcategories, preserving the joint or fusing the joint. The surgery ischosenbased on the above mentioned staging system. </p> 
        <h3>Joint preserving:</h3>  
        <ul> <li itemprop="possibletreatment">Chielectomy: remodeling of the joint with removal of the bone spurs and freeing theadheredtissue.</li> <li itemprop="possibletreatment">Shortening osteotomy: Cuts in the metatarsal bone to "decompress" the joint.</li> </ul>
        <span class="thumbnail text-muted center"><img src="/images/pathology/xraypre.jpg" alt="Bone spur formation in hallux limitus/rigidus" class="img img-responsive"/><small>Arrow indicates large bone spur</small></span>

        <span class="thumbnail text-muted center"><img src="/images/pathology/xraypost.jpg" alt="Appearance following Chielectomy" class-"img img-responsive"/>
          <small>Arrow indicates area of spur removal</small></span>
          <h3>Joint Destructive:</h3>  
          <ul> 
            <li itemprop="possibletreatment">Fusion: Removal of the remaining cartilage, removal of the bone spurs and fusion of the metatarsal to the proximal phalanx. This results in a stiff big toe joint which generally no longer has pain. Patient can usually return to most activities including running and other high impact sports once they arecompletely healed.</li> 
          </ul> 
          
