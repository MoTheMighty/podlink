         <h3>Definition:</h3> 
         <p>Stiffness and pain of the big toe joint is termed Hallux Rigidus or Hallux Limitus. Hallux is the medical term for the big toe and the terms rigidus and limitus describe the extent to which the toe can move.</p> 
         <p style="text-align: center;"><img src="/images/pathology/chilectomypost.jpg" alt="One month post op following chielectomy for hallux rigidus/limitus" /></p> 
         <p style="text-align: center;">Typical appearance of forefoot 1 month following surgery for Hallux Limitus</p> 
         <h3>Cause:</h3> 
         <p itemprop="cause">Abnormal alignment of the metatarsal bone relative to the proximal phalanx (big toe bone) is the most common underlying factor in developing Hallux Limitus/Rigidus. During a normal stride, whether walking or running, the proximal phalanx must most<strong> up and over </strong>the metatarsal. In order to do this, the metatarsal bone must be slightly lower then the proximal phalanx. If the metatarsal bone is slightly elevated (or higher) then the proximal phalanx, the big toe cannot move up and over the metatarsal. Instead, a jamming of the joint occurs. Repeated jamming over a period of years leads to degeneration of the joint. <span itemprop="naturalprogression">Cartilage is worn away and large bone spurs form on the top of the joint. The combination of cartilage loss and bone spurring causes loss of motion of the joint.</span></p> 
         <h3>Diagnosis:</h3> 
         <p>Diagnosis is usually made via clinical exam and x-rays. Sign and symptoms include:</p>  
         <ul> <li itemprop="signorsymptom">Spasms of the overlying tendons</li> <li itemprop="signorsymptom">Inability to move the big toe joint without pain</li> 
         <li itemprop="signorsymptom">Unable to wear high heel shoes without pain in the great to joint</li> 
         <li itemprop="signorsymptom">Unable to run without pain in the big to joint</li> 
         <li itemprop="signorsymptom">Patient must turn their foot slightly sideways when walking to prevent pain</li> 
         <li itemprop="signorsymptom">Painful Bump on the top of the big toe joint</li> 
         </ul> 
         <h3>Staging / Classification:</h3> 
         <p>Hallux Limitus/Rigidus in a progressive problem, meaning as time goes on it tends to get worse. There are a defined set of stages to this condition. Each stage generally progress to the next as a function of time. We use the staging system to help determine the best course of treatment.</p>
         <ul> 
         <li itemprop="stage">Grade 1: Decreased motion to the big toe joint, with or without pain. Recent onset. X-rays show no changes, there are no spurs to the top of the joint.</li> 
          
         <li itemprop="stage">Grade 2: Decreased motion, pain is present. A bump is usually present on the top of joint and the X-rays show bone on bone with spurs.</li> 
         <span class="thumbnail text-muted center"style="width:300px"><img src="/images/pathology/halluxLimitusStageone.jpg" alt="Stage One Hallux Limitus" class-"img img-responsive"/>
          <small>Circle shows joint space narrowing and spurring</small></span>
         <li itemprop="stage">Grade 3: Very limited range of motion to the big toe joint. A grinding sensation is present when trying to move the joint. Redness and pain with most activities. X-rays show advanced bone on bone changes, minimalcartilageis left within the joint.</li> 
         <li itemprop="stage">Grade 4: Big toe joint does not move. Pain with activities. X-rays show no joint space, complete loss of cartilage.</li> 
         </ul> 
         <span class="thumbnail text-muted center"><img src="/images/pathology/stage4.jpg" alt="Stage One Hallux Limitus" class-"img img-responsive"/>
          <small>Arrows indicate joing collapse</small></span> 
       </div>
