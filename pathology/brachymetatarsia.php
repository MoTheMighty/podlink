        <small>Shortened fourth metatarsal</small></span> 
    <h3>Tension Stress Effect</h3>
      <p>We owe a great deal of acknowledgment to the work done by the Russians regarding ground breaking work in the area of treating severe structural abnormalities utilizing a concept known as the the "tension stress effect."  This concept, developed by <a href="http://www.ilizarov.com/en/who-is-ilizarov/">Dr.  Ilizarov</a>, focused on the bodies ability to "grow" new bone in a widening distraction gap under controlled mechanical means.</p> 
      <span class="thumbnail text-muted center"><img src="/images/i.jpg" alt="Dr. Ilizarov" class-"img img-responsive"/>
        <small>Dr. Ilizarov and patient</small></span> 
      
