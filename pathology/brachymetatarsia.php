      <h3>Surgical Details</h3>
      <h4>Goals</h4>
        <ul>
          <li>Alleviate pain</li>
          <li>Restore a functional metatarsal parabola</li>
          <li>Establish a cosmetically acceptable foot</li>
        </ul> 

      <p>Proper communication between the podiatric surgeon and the patient is critical.  The patient will play an active role in the surgical outcome and commitment to perform daily distraction is critical.  <a href="http://www.jfas.org/article/S1067-2516(98)80015-X/abstract"> Nicotine use has been shown to impede bone healing.</a>  Since this procedure involves a complex biological cascade of events with each step dependent on the preceding, smoking cessation is required.</p>

      <p>A mini external fixator is place over the bone to be lengthened.  A small surgical cut in the bone is performed.  After 7-14 days, the patient will turn a tiny wheel on the mini fixator twice a day.  Turning the wheel will slowly move the bone.  The gap that is created will fill in with newly grown bone.  The number of days required to distract the bone is dependent on the amount of lengthening required.  A good rule of thumb is about 0.5 cm (0.2 inches) of lengthening per day.</p>

