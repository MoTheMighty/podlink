    <h3>Brachymetatarsia</h3>
      <p>This condition refers to an abnormally shortened metatarsal bone.  This is generally caused by a premature closure of the growth plate of the metatarsal.  While the remaining metatarsals continue to grow at a normal rate, the affected bone will remain the same size.  With time, this causes a visible difference in the appearance of the toes around the time of early puberty.</p>
      <p>Most commonly, the fourth metatarsal is affected, resulting in a the appearance of a short forth toe.</p>
        
