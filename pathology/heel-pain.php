<p>The most common reason patients seek our care is for heel pain. This condition can be caused by a variety of factors. Including, entrapped nerves, metabolic disorders, arthritis and infections. By far the most common cause, accounting for 95% of all cases, is abnormal, repetitive stresses placed on the anatomic structures of the foot and leg resulting in injury and inflammation. This condition is known as <span itemprop="name">plantar fasciitis</span>.</p>
<p>The <span itemprop="associatedAnatomy" itemscope itemtype="http://schema.org/AnatomicalStructure"><span itemprop="name">plantar fascia</span></span> has several functions. It helps keep the small muscles of the foot in a compact and efficient package beneath the foot when full weight is bore. More importantly, it helps <span itemprop="associatedAnatomy">maintain the bones of the foot in an "arch" orientation. As body weight is transmitted down through the leg and ankle, the foot tries to maintain a triangle shape.</span> The base of the triangle is the plantar fascia. The fascia must resist the force of the heel and arch bones to flatten out. The extent to which the arch flattens is dependent on how much the plantar fascia can support. Thus, during the normal course of walking, our plantar fascia is subjected to tremendous, repetitive forces.</p>
<p>To understand what is causing heel pain, an examination of the anatomy is necessary. The plantar fascia is a dense thin layer of tissue just beneath the skin on the sole of the foot. It is shaped like a triangle (blue lines), with the apex oriented towards the back of the foot.</p>

