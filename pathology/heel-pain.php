        <div class="page-header">
          <h3>Signs and Symptoms</h3>
        </div>

        

        <p itemprop="signOrSymptom">Patients present with chronic pain to the bottom of the heel. Usually, this pain has been  present for weeks to months and they have delayed seeking treatment hoping the symptoms would subside. Terms such as a stone bruise or deep ache are usually used  to describe the discomfort. The onset is usually gradual, with no recollection of an injury  or accident.</p>
        <p>Classically, the <span itemprop="signOrSymptom">pain is worse when arising from rest. For example, the first few steps  taken after waking in the morning or after a prolonged period of sitting can generate the  most pain.</span> Once the foot warms up, the heel pain may lessen. Swelling or puffiness on  the inside of the heel may also be noticed.</p>


        
        <div class="page-header">
          <h3>Treatment</h3>
        </div>   
        <div class="well center">
          <h4 itemprop="possibletreatment">"Treatment targets the plantar fascia, not the heel spur, since it is the true source of the  pain."</h4>
        </div>

        <p itemprop="expectedprognosis">For the vast majority of patients (90%), conservative treatment can eliminate their pain. But this does not happen overnight.  Rare, are the cases where one visit to the Podiatrist can completely alleviate their symptoms.</p>  
        <p>We tailor our treatment to the individual patient, taking into account health, lifestyle and their work environment. A cookie cutter approach will not be as effective, as there are many factors which must be considered. In general, the steps outlines below are utilized in different combinations and varying degrees of emphasis.</p>

        <hr>

        <h4>Anti-inflammatory Medication:</h4>
        <p itemprop="possibleTreatment">Also known as NSAID's, this class of drugs helps to decrease the inflammatory process which maybe occurring around the damaged plantar fascia. These medications should only be used for very short periods of time and the risks vs. the benefits must be weighed with each individual patient.  Serious complications can occur using this class of medicine.
          <hr>

          <h4>Stretching:</h4>

          <p itemprop="possibletreatment">By stretching, if by far the most important part in the resolution of this condition. <a href="/resources/pfstretches.pdf"> Stretching instructions</a> are to be performed on a daily basis.  Skipping this step and relying on medications alone will not result in a long term resolution. Night splints are also an integral part in stretching the plantar fascia, Achilles tendon and calf muscle.</p>
          <hr>

          <h4>Injections:</h4>

          <p itemprop="possibletreatment">Injection into the plantar fascia of a long acting steroid provides potent anti-inflammatory action. They are used for moderate  to severe cases of plantar fasciitis. Plantar fasciitis which does not respond to other  forms of treatment is also an indication for injection therapy.  <span itemprop="possiblecomplication">Although no stead fast rule exist as to the number of injections you can receive, be aware the multiple steroid injections into the same anatomical area can result in permanent tissue damage.</span></p>
          <hr>

          <h4>Arch Supports:</h4>
          <p itemprop="possibletreatment">These devices help to support the arch and take pressure off the plantar fascia. They  are placed in shoe gear and can help prevent recurrence of the condition. Both custom  molded orthotics and over-the-counter devices can be effective.</p>
          <hr>

          <h4>Shoe gear:</h4>
          <p itemprop="possibletreatment">Proper fitting and supporting shoe gear is critical is alleviating heel pain. Many times we  place our patients in athletic shoes for prolonged periods. These styles allow more  support to the arch and heel, creating a favorable environment for recovery. Do not use flats while trying to recover from plantar fasciitis.</p>
          <hr>

          <h4>Rest:</h4>
          <p itemprop="possibletreatment">In severe cases of heel pain, we ask our patient to take time off work to rest their foot.  This is especially true for people who spend a great deal of time on their feet. This may  be combined with immobilization in a short leg cast.</p>
          <hr>

          <h4>Surgery:</h4>
          <p itemprop="possibletreatment">Surgery maybe needed for those cases of plantar fasciitis which have failed all forms of conservative treatment. There are many different forms of surgery for plantar fasciitis, each of our physicians has his preferred method.</p>
          <p>I prefer to perform an "instep plantar fasciotomy."  This procedure has a good success rate which has been proven in the medical literature.  It is, however, not without some risks.  <span itemprop="possiblecomplication">Calcaneal-cuboid joint syndrome is a possible complication with any surgery where the plantar fascia is transected.  Although rare, it does occur.</span></p>

          <hr>

