<p> Since the plantar fascia is shaped like a triangle, a great deal of force is concentrated at the heel. When too much strain is placed on the fascia, the area of greatest strain becomes inflamed. This inflammatory process leads to pain and swelling. This is often termed plantar fasciitis or heel spur syndrome. Over time, this condition may produce bone projections off the heel known as spurs. Often times, patients will come to the office with heel pain wishing their "spurs" be removed. They believe the heel pain is caused by these bony growths, when in fact, the true reason for the pain is from inflammation of their plantar fascia. The heel spur has developed in response to the chronic irritation of the plantar fascia on the calcaneus (heel bone). </p>
