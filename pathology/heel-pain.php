<h3>Signs and symptoms</h3>

<p><span itemprop="signOrSymptom" itemscope itemtype="http://schema.org/MedicalSymptom"><span itemprop="name">Patients present with chronic pain to the bottom of the heel. Usually, this pain has been  present for weeks to months and they have delayed seeking treatment hoping the symptoms would subside. Terms such as a stone bruise or deep ache are usually used  to describe the discomfort. The onset is usually gradual, with no recollection of an injury  or accident.</p>
<p>Classically, the <span itemprop="signsOrSymptoms">pain is worse when arising from rest. For example, the first few steps  taken after waking in the morning or after a prolonged period of sitting can generate the  most pain.</span> Once the foot warms up, the heel pain may lessen. Swelling or puffiness on  the inside of the heel may also be noticed.</p></span></span>

<p><h3>Treatment:</h3><p>
<p>For the vast majority of patients (95%), conservative treatment can eliminate their pain. We tailor our treatment to the individual patient, taking into account health, lifestyle and their work environment. A cookie cutter approach will not be as effective, as there are many factors which must be considered. In general, the steps outlines below are utilized in different combinations and varying degrees of emphasis.</p>

<hr>

<p><span class="label label-primary">Anti-inflammatory Medication</span><p>
<p>Also known as <span itemprop="possibleTreatment">NSAID's, this class of drugs helps to decrease the inflammatory process  within the plantar fascia. We frequently use these drugs for a period of several months.</span></p>
<hr>

<p><p><span class="label label-primary">Stretching:</span><p></p>
<p style="text-align: center;"> <iframe width="300" height="182" frameborder="0" src="http://www.youtube.com/embed/hauyuX-uCq8"></iframe> </p>
<p>By stretching the plantar fascia, the abnormal pressure can be alleviated and resolution of the problem obtained. Many times night splints can achieve this goal with minimal pain and inconvenience.</p>
<hr>

<p><span class="label label-primary">Injections:</span><p>

<p>Cortisone injections carry potent anti-inflammatory action. They are used for moderate  to severe cases of plantar fasciitis. Plantar fasciitis which does not respond to other  forms of treatment are also an indication for their use.</p>
<hr>

<p><span class="label label-primary">Orthoses (arch supports):</span><p>
<p>These devices help to support the arch and take pressure off the plantar fascia . They  are placed in shoe gear and can help prevent recurrence of the condition. Both custom  molded orthotics and over-the-counter devices can be effective.</p>
<hr>

<p><span class="label label-primary">Footwear:</span><p>
<p>Proper fitting and supporting shoe gear is critical is alleviating heel pain. Many times we  place our patients in athletic shoes for prolonged periods. These styles allow more  support to the arch and heel, creating a favorable environment for recovery. </p>
<hr>

<p><span class="label label-primary">Rest:</span><p>
<p>In severe cases of heel pain, we ask our patient to take time off work to rest their foot.  This is especially true for people who spend a great deal of time on their feet. This may  be combined with immobilization in a short leg cast.</p>
<hr>

<p><span class="label label-primary">Surgery:</span><p>
<p>Surgery maybe needed for those cases of plantar fasciitis which have failed all forms of conservative treatment. There are many different forms of surgery for plantar fasciitis, each of our physicians has his preferred method.</p>

