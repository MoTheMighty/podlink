              <div class="tab-pane fade" id="treatment">
                  <p itemprop="possibletreatment">Treatment of ankle fractures depends upon the type and severity of the injury. At first, the Podiatrist will want you to follow the R.I.C.E. protocol:</p> <ul> <li>Rest: Stay off the injured ankle. Walking may cause further injury.</li> <li itemprop="possibletreatment">Ice: Apply an ice pack to the injured area, placing a thin towel between the ice and the skin. Use ice for 20 minutes and then wait at least 40 minutes before icing again.</li> <li>Compression: An elastic wrap should be used to control swelling.</li> <li itemprop="possibletreatment">Elevation: The ankle should be raised slightly above the level of your heart to reduce swelling.</li> </ul> <p>If no surgical intervention is required, the extremity is generally placed in a cast or fracture boot for 6 to 8 weeks. Weight bearing will be initiated once the podiatrist determines that sufficient healing has taken place to allow the ankle to tolerate weight. If surgery is required, generally, plates and screws will be inserted into the bones to reconstruct the ankle joint. Recovery times will be determined by the foot and ankle surgeon based on numerous factors. Physical therapy is generally required to help restore normal range of motion and strength to your ankle joint.</p> <p itemprop="expectedprognosis">If an ankle fracture is treated promptly and properly, the prognosis is good. Overall, about 80% of all sports-related ankle fractures heal without any long-term complications. However, as with any significant injury near a joint, arthritis may develop years later.</p>
