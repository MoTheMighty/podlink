                  <p>If you have a fractured ankle, your podiatric symptoms probably may include:</p> <ul> <li itemprop="signorsymptom">Pain at the site of the fracture, which in some cases can extend from the foot to the knee</li> <li itemprop="signorsymptom">Significant swelling, which may occur along the length of the leg or may be more localized</li> <li itemprop="signorsymptom">Blisters may occur over the fracture site. These should be promptly treated by a foot and ankle surgeon.</li> <li>Bruising that develops soon after the injury</li> <li itemprop="signorsymptom">Inability to walk—however, it is possible to walk with less severe breaks, so never rely on walking as a test of whether a bone has been fractured</li> <li itemprop="signorsymptom">Change in the appearance of the ankle – it will look different from the other ankle</li> <li>Bone protruding through the skin—a sign that immediate care is needed. Fractures that pierce the skin require immediate attention because they can lead to severe infection and prolonged recovery.</li> </ul> <h3>Diagnosis</h3> <p>A Thorough podiatric history and physical examination along with x-rays of the injured ankle are generally used to make the diagnosis and determination if surgical management is necessary.</p>
