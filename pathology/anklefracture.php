                  <p>To understand an ankle fracture, first we must define which bones make of the ankle joint.</p> <div style="float: right;"><img alt="ankle joint anatomy" src="/images/pathology/anklejoint.jpg" /></div> <ul itemprop="associatedanatomy"> <li><strong>The tibia.</strong> This is the largest bone of your leg and is also know as your shin bone. The tibia's lower end curves around the inside of you ankle to become a structure called the medial malleolus, which you can feel at the inside of your ankle.</li> </ul> <ul> <li><strong>The fibula.</strong> This is the thinner of the two bones of the lower leg. It starts just below your knee and course down the outside of your leg to bone a structure called the lateral malleolus, which you can feel on the outside of your ankle.</li> </ul> <ul> <li><strong>The talus.</strong> This is a wedge-shaped bone that is located deep inside the ankle, located between the heel bone and the ends of the tibia and fibula. The talus supports the lower ends of the tibia and fibula. You generally cannot feel this bone as it is deep in the ankle.</li> </ul> <p>There are many ways to fracture an ankle bone, the most common injuries involve a sudden sharp twist of the ankle. Ankle fractures are common injuries among people of all ages, interests and lifestyles. People involved in a wide range of athletic activities, including basketball players, snowboarders, soccer players and football players are at high risk of ankle fractures because of the physical demands placed on their ankles. Ankle fractures also occur during slips on icy pavement, falls from a high place, or direct impacts to the ankle during a car crash or motorcycle accident. High-impact ankle injuries are especially dangerous if the bone pokes through the skin and is exposed to the enviroment as this can allow the area to become contamined and lead to an increased chance of infection.</p>
