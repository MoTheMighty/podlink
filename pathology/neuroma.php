                <div class="tab-pane fade" id="treatment2">
                               <p>Neuromas have typically been very challenging to treat. Whenever we are dealing with a nerve, any treatment must be aimed at not causing additional irritation. In the late part of the 1990’s we began to read reports of a new treatment for this tough problem.</p> <p>A novel approach was being taken, where a dilute solution of alcohol was injected into the diseased nerve. Initial reports at scientific meetings were promising. After publication of the Dr. Dockery’s work confirming success of the new technique, Drs. Richard Perez and Walter Strash became the first podiatrist in San Antonio to offer this new technique.</p> <p>Following the recognized protocol, we achieved similar success to what was described in the study. Since that time, we have also incorporated the use of diagnostic ultrasound, which allows direct visualization of the nerve bundle.</p> <p>The alcohol solution is injected into the nerve on a weekly basis. The alcohol molecules displace the abundant water molecules present within the nerve fibers. This has the affect of slowly shrinking the nerve. And with time, the symptoms disappear.</p> <p><strong>Procedure:</strong></p> <ul> <li>The patient will require 7 weekly injection of 4% ETOH. These are performed under ultrasound guidance, as the medicine is infiltrated directly into the nerve.</li> <li>During the treatment period, the patient can continue to participate in all activities, including sports.</li> <li>There is no restriction on shoe gear during treatment. </li> </ul> <h3>Success Rate</h3> <p>Studies report success rates (complete pain relief) of between 61% and 89%. All studies report very few side effects.</p> <h3>Medical References </h3> <ol> <li><citation>Efficacy of chemical neurolysis for the treatment of interdigital nerve compression of the foot: a retrospective study. Mozena JD, Clifford JT. J Am Podiatr Med Assoc. 2007 May-Jun;97(3):203-6.</citation></li> <li><citation>Treatment of Morton's neuroma wi