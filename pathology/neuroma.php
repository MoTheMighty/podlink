                      <p itemprop="associatedanatomy">An intermetatarsal neuroma is any irritative process of the common digital nerve branch that supplies the plantar (bottom) of adjacent toes. It most commonly involves the nerve that supplies sensation to adjacent sides of the third and fourth toes, but can also affect other toes of the foot.</p><p>A neuroma is not cancerous and is not a true tumor but a reactive, degenerative process such as a scar. Therefore, a neuroma is benign inflammation  (enlargement) of the nerve. </p>

                      
                       

                      <p itemprop="epidemiology">Neuromas occur in all adults and are most common in females. Several factors contribute to its occurrence:</p>
                       <ul> 
                       <li itemprop="cause">High heeled shoes </li> 
                       <li itemprop="cause">trauma </li> 
                       <li itemprop="cause">inflammatory conditions such as arthritis</li> 
                       <li itemprop="cause">repetitive trauma from stresses incurred in occupational and recreational activities.</li> 
                       </ul> 
                       <p itemprop="cause">Any condition that causes constriction or irritation to the nerve can lead to the development of a neuroma.  As example, wearing shoes, especially dress shoes, causes significant squeezing of the forefoot.  This, in turn, leads to compression of the nerves and eventually, a neuroma can form.</p><span class="thumbnail"><img src="/images/pathology/footshoexray.jpg" alt="xray of foot in shoe" class="img img-responsive"><p class="text-muted small center">Note left X-Ray showing compression of forefoot bones</p> </span>   
