                      <h3>Treatment </h3><p itemprop="possibletreatment"><p>The primary goal of treatment is to reduce or eliminate symptoms, so patients can maintain a normal lifestyle.</p><p>Treatment may be surgical or non-surgical. Non-surgical treatment consists of treatments such as, steroid injections, sclerosing alcohol injections, changing shoe gear style, physical therapy  and prescription orthosis. <span itemprop="possibletreatment">Injection therapy with 4% alcohol sclerosing agent has proven very successful in the treatment of intermetatarsal neuromas. A study from the November/December 1999 issue of The Journal of Foot &amp; Ankle Surgery quoted an 89% success rate with the use of 4% alcohol sclerosing solution.</span> Similar results in our practice have decreased the number of neuroma surgeries being performed. The decision to surgically intervene is based upon the patients symptoms, the judgment of your foot and ankle surgeon and your preference. <span itemprop="possibletreatment">A neurectomy or surgical excision of a neuroma is performed when conservative treatment proves ineffective.</span></p>
