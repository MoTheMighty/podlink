              <div class="tab-pane fade" id="treatment">
                      
There are a variety of methods to treat a tarsal coalition depending on the severity of the condition, the age of the patient, and which joint or bones are effected. Conservative treatment involves non-surgical otpions. Conservative treatment is directed toward reducing motion in the affected joint to decrease the pain and muscle spasm. Immobilization in a cast or fracture walker boot can calm down the irritated areas and reduce spasms.  Orthotics ( custom shoe inserts) are also commonly used to accomplish this on a long term basis to help prevent recurrence of symptoms.  Physical therapy and anti-inflammatory medication may be utilized as well. Cortisone injections in the affected area may provide relief for an indefinite period of time. These conservative methods of treatment may or may not provide long-term relief. <br /> <br />
If symptoms persist then surgical correction is often a good option for long term relief. The type of surgery will depend on the type of coalition, its location, how high or low the arch is and the amount of arthritis and joint damage that has been caused in the foot. If caught early the coalition can be removed to restore motion of the foot. Many times patients do not seek treatment until there has been significant joint damage. In these situations surgery may involve fusing the affected affected joint or surrounding joints. This is designed to limit or completely stop the pain and the painful motion of the affected joints.</p>
