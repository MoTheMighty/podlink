              <div class="tab-pane fade" id="signs_symptoms">
                Patients with a painful tarsal coalition commonly describe an aching sensation deep in the foot near the ankle or arch. In many cases, muscle spasms on the outside of the affected leg are present. This a natural reaction of the body as it tries to limit the painful motion occurring in the foot. Patients may notice that the affected foot is not as flexible and appears significantly more flattened when compared to the other foot. This only holds true if only one foot is affected, as it is common for both feet to be affected. All flat-footed people do not have tarsal coalitions though. There are many other causes of flat feet as well.</p> <p> </p> <div style="text-align: center;"><br /> </div> <p>
                Symptoms most commonly appear in the teenage or early adult years depending on the location of the coalition. It should be noted that not all tarsal coalitions become symptomatic I see many patients that never knew they had a tarsal coalition until it was seen on X-rays taken for other problems. The onset of symptoms may be delayed later into adulthood as well as the joints become more affected with age.</p> <p><br /> </p> <h3>
                Diagnosis</h3> <p>Diagnosis of a tarsal coalition can usually be made from symptoms described by the patient to the doctor and physical exam findings. X-rays are usually taken and in most cases a CT scan or MRI will confirm the diagnosis and provide valuable information regarding the type of coalition, its location, and how the joints have been affected.</p> <p> <div style="text-align: center;"><img src="/images/pathology/FOTA55E.jpg" alt="Calcaneal Navicular Bar / Coalition" /><br /> </div> </p>
