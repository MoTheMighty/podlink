                  <p>
                    A tarsal coalition is a bone condition in feet that causes decreased motion or absence of motion in one or more of the joints in the foot. The lack of motion or absence of motion is due to abnormal bone, cartilage or fibrous tissue growth across a joint. When excess bone has grown across a joint there is usually little or no motion in that joint. Cartilage or fibrous tissue growth can restrict motion of the affected joint to varying degrees causing pain in the affected joint or in surrounding joints.<br /> <br />
                    Tarsal coalitions can occur outside of a joint as well. This is referred to as a bar. A bar connects two bones that don't normally touch or have a joint between them. The bar will limit motion in the surrounding joints causing abnormal wear and tear to the joints of the foot. This can lead to early arthritis and pain. The bar itself can be painful as well if it is incomplete, traumatized during walking, sporting activities, or an accident.  <br /> <br />
                    In the foot, the bones found at the top of the arch, the heel and below the ankle are referred to as the tarsal bones. A tarsal coalition is an abnormal connection between two of the tarsal bones in the back of the foot or the arch. This abnormal connection between two bones is most commonly an inherited trait and passed down from generation to generation. All coalitions are not inherited though. They can also arise from outside sources such as arthritis, infection, trauma and abnormal bone growth. These outside causes are much less frequent.
