          <h3>Symptoms</h3>
        </div>
        <p>When you damage the peroneal tendons from an acute injury, it may not be readily apparent as there is generally other injuries which are the primary focus, such as torn ligaments in the case of an ankle sprain.</p>  
        <ul> 
          <li itemprop="signorsymptom">Swelling around the outside of the ankle</li> <li>Redness and warmth</li> 
          <li itemprop="signorsymptom">Pain with walking, running or moving the foot from side to side</li> 
          <li itemprop="signorsymptom">Instability or weakness the foot of ankle</li> <li>Inability to pivot</li> 
        </ul>
        <div class="page-header">
          <h3>Treatment</h3>
        </div>
        <ul>
          <li itemprop="possibletreatment"><strong>Immobilization:</strong> A cast or splint may be used to keep the foot and ankle from moving and allow the injury to heal.</li> <li itemprop="possibletreatment"><strong>Medications:</strong> Oral or injected anti-inflammatory drugs may help relieve the pain and inflammation.</li> <li itemprop="possibletreatment"><strong>Physical therapy:</strong> Ice, heat, or ultrasound therapy may be used to reduce swelling and pain. As symptoms improve, exercises can be added to strengthen the muscles and improve range of motion and balance.</li> <li><strong>Bracing:</strong> The podiatrist may provide a brace to use for a short while or during activities requiring repetitive ankle motion. Bracing may also be an option when a patient is not a candidate for surgery.</li> <li itemprop="possibletreatment"><strong>Surgery:</strong> Surgery may be needed to reconstruct the damaged or torn tendons.</li> </ul>


