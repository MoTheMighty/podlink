      <div style="float: right;"><img alt="" src="/images/pathology/peronealtendon.jpg" /></div> <p itemprop="associatedanatomy">The peroneal tendons consist of two tendon that arise from the muscles on the outside of the leg just below your knee. The tendons course behind the outside of the fibula(outside ankle bone), then turn and connect to two different areas of the mid foot. One peroneal tendon attaches to the outer part of the mid foot, while the other tendon runs under the foot and attaches near the inside of the arch. The main function of the peroneal tendons is to stabilize the outside of foot and ankle.</p> <p itemprop="cause">Peroneal tendon problems occur either from repetitive use injuries such as running, tennis, cycling or from a sudden injury such as an ankle sprain or fracture. In the case of repetitive use, patients will usually notice a gradual increase in symptoms to the outside of the ankle which are usually made worse by doing physical activities. Initially, the tendon will become inflamed a condition known as tendonitis. However, if not treated, the tendons generally become scarred and weak and will eventually tear.</p> <p>Being on the edge of the hill country, San Antonio is a hotspot for mountain biking and cycling.  Consequently, many of the the sports related peroneal tendon injuries we see as podiatrist are a result of those activities.
