                    <h3>Treatment</h3> <p>Unfortunately, warts on the foot are EXTREMELY difficult to resolve. It is typical that most treatments have a 30%-40% failure rate. Generally, several attempts must be made to resolve the problem.  Some treatments which have been used include, surgical removal, lasers, cryosurgery,  kerolytic compounds (salicylic acid), dried extract of a blister beetle, Trichloractetic acid, immunomodulators (Aldara, 5-fluorouracil), bleomycin, cimetadine and duct tape.</p> <p>Despite this extensive and diverse list of possible treatment modalities, there is little consensus as to a gold standard treatment.</p> 

