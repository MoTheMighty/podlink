                   <p>There are several complaints, as podiatrist in San Antonio, that we see over and over. Warts (foot warts, plantars wart, verrcua plantaris) are just such a problem. Warts are a non-cancerous growths of the skin. They are caused by the Human Papillomavirus (HPV) . HPV describes a broad category of viruses which cause various infection. We are concerned with a very small subset of the HPV family, specifically, HPV 1, 3, 27, 29 and 57 are known to cause warts on the foot.</p> <p>The HPV only infects the superficial structures of the skin, and thus does not invade any other body parts. The warts typically occur on areas of the foot that are under direct pressure, such as the ball and heel of the foot. However, they can also be seen under and around toenails, as these structures tend to be under pressure from shoe gear.</p> <h3>Cause</h3> <p>Warts infect the skin by penetrating microscopic cuts in its outer layers. The HPV virus can live for many months of moist surfaces. Hence, gyms, showers, sharing of shoes and other moist areas can harbor the virus. Once the virus enters the skin, symptoms will usually be noticed within several weeks. It is estimated that up to 12% of the world's population and 20% of school aged children are infected with this type of HPV wart. Most people who are infected with the virus will have spontaneous resolution within  6 months.</p> <h3>Risks factors</h3> <ul> <li>Using public showers and gym equipment without shoes</li> <li>Weakened immune system</li> <li>Breaks or cuts in the skin on the bottom of the foot. </li> </ul> <p style="text-align: center;"><img src="/images/pathology/wart.jpg" alt="plantar wart to little toe" /></p> <p style="text-align: center;">Typical appearance of a wart to the small toe.</p> <h3>Symptoms:</h3> <p>Generally, patients complain of the painful growth of the bottom of their foot which often times looks like a callus. It is not usually, to see dark specs within the "callus."  These are often confused with a possible retained foreign material such as a thorn of a piece of glass. However, these spots are small blood vessels which feed the wart. As San Antonio Podiatrist, we often look for these spots to help diagnosis the condition.</p> <p>Patient may also note bleeding from the wart when they pick or scratch at it.</p>
