     <div class="page-header">
      <h3>Treatment</h3> 
    </div>
    <p itemprop="expectedprognosis">Unfortunately, warts on the foot are EXTREMELY difficult to resolve. It is typical that most treatments have a 30%-40% failure rate. Generally, several attempts must be made to resolve the problem.  Some treatments which have been used include:
    <ul>
    <li itemprop="possibletreatment">Surgical removal,</li> 
    <li itemprop="possibletreatment">Lasers,</li> 
    <li itemprop="possibletreatment">Cryosurgery,</li>  
    <li itemprop="possibletreatment">Kerolytic compounds (salicylic acid),</li> 
    <li itemprop="possibletreatment">Dried extract of a blister beetle,</li> 
    <li itemprop="possibletreatment">Trichloractetic acid,</li> 
    <li itemprop="possibletreatment">Immunomodulators (Aldara, 5-fluorouracil), Bleomycin, Cimetadine</li> 
    <li itemprop="possibletreatment">Duct tape</li>
    </ul>
    </p> <p>Despite this extensive and diverse list of possible treatment modalities, there is little consensus as to a gold standard treatment.</p> 


