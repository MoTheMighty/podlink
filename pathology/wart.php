     <h3>Symptoms:</h3> 
     <p itemprop="signorsymptom">Generally, patients complain of the painful growth of the bottom of their foot which often times looks like a callus. It is not usually, to see dark specs within the "callus."  These are often confused with a possible retained foreign material such as a thorn of a piece of glass. However, these spots are small blood vessels which feed the wart. We often look for these spots to help diagnosis the condition.</p> 
     <p itemprop="signorsymptom">Patient may also note bleeding from the wart when they pick or scratch at it.</p>
