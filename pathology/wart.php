      <div class="page-header">
        <h3>Cause</h3> 
      </div>

      <p>There are several complaints, as podiatrist in San Antonio, that we see over and over. Warts (foot warts, plantars wart, verrcua plantaris) are just such a problem. <span itemprop="cause">Warts are a non-cancerous growths of the skin. They are caused by the Human Papillomavirus (HPV) . HPV describes a broad category of viruses which cause various infection. We are concerned with a very small subset of the HPV family, specifically, HPV 1, 3, 27, 29 and 57 are known to cause warts on the foot.</span></p> 
      <p itemprop="pathophysiology">The HPV only infects the superficial structures of the skin, and thus does not invade any other body parts. The warts typically occur on areas of the foot that are under direct pressure, such as the ball and heel of the foot. However, they can also be seen under and around toenails, as these structures tend to be under pressure from shoe gear.</p> 

      <p itemprop="cause">Warts infect the skin by penetrating microscopic cuts in its outer layers. The HPV virus can live for many months of moist surfaces. Hence, gyms, showers, sharing of shoes and other moist areas can harbor the virus. Once the virus enters the skin, symptoms will usually be noticed within several weeks. <span itemprop="epidemiology">It is estimated that up to 12% of the world's population and 20% of school aged children are infected with this type of HPV wart. Most people who are infected with the virus will have spontaneous resolution within  6 months.</span></p> 
      <div class="page-header">
        <h3>Risk Factors</h3> 
      </div>
      <ul> 
       <li itemprop="riskfactor">Using public showers and gym equipment without shoes</li> 
       <li itemprop="riskfactor">Weakened immune system</li> 
       <li itemprop="riskfactor">Breaks or cuts in the skin on the bottom of the foot. </li> 
     </ul> 
     <span class="thumbnail text-muted center"><img src="/images/pathology/wart.jpg" alt="plantar wart to little toe" class-"img img-responsive"/>
