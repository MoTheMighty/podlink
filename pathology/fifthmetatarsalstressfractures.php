 <h3>5th Metatarsal Fracture</h3> <p><img width="215" height="265" class="border-right" alt="Fifthmetafinalillus" src="/images/pathology/fifthmetafinalillus1.jpg" />
Two types of fractures that often occur in the fifth metatarsal are:</p> <ul> <li><strong>Avulsion fracture</strong>. In an avulsion fracture, a small piece of bone is pulled off the main portion of the bone by a tendon or ligament. This type of fracture is the result of an injury in which the ankle rolls. Avulsion fractures are often overlooked when they occur with an ankle sprain.</li> <li><strong>Jones fracture</strong>. Jones fractures occur in a small area of the fifth metatarsal that receives less blood and is therefore more prone to difficulties in healing. A Jones fracture can be either a stress fracture (a tiny hairline break that occurs over time) or an acute (sudden) break. Jones fractures are caused by overuse, repetitive stress, or trauma. They are less common and more difficult to treat than avulsion fractures.</li> </ul>

