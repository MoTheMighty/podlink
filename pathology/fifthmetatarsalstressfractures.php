 <h3>Metatarsals</h3> 

<p>Metatarsal are the bones in the mid-foot portion of your foot, just behind the toes.  They are responsible for giving your foot its overall shape.  They are also the most commonly broken foot bone.  There are five metatarsals, and they are numbers from left to right 1 through 5.  The outside metatarsal, also known as the fifth metatarsal, is the most frequently fractured (broken) foot bone.  </p>
<p>Because of its unique position on the outside of the foot, relatively minor twists can result in a fracture.  Stress fractures of the fifth metatarsal are also a common occurrence, however, less so then other areas of the foot.</p>
<h3>Anatomy:</h3>
<p>The anatomy of the fifth metatarsal also contributes to frequent injuries.  At its base, a large tendon, the peroneus brevis provides a strong pull to the base of the bone.  The bone then narrows, forming a long slender cylinder.</p>
<span class="thumbnail text-muted center"><img src="/images/5thmet.jpg" alt="diagram of fifth metatarsals injury zone" class="img img-responsive"/><small>Areas of injury</small></span>

<h3>Injury:</h3>
<p>Injury occurs with a sudden twist of the foot.  Generally, a snap or popping sensation is felt.  This is followed by swelling and bruising.  Usually, there is pain with walking.
There are distinct zones of injury.</p>
<p><strong>Tuberosity fracture:</strong>  These fractures are the most common and result from the peroneus brevis forcefully pulling the base of the bone.  Base fractures are usually treated with immobilization and crutches.  If the broken bone has shifted, surgery will be needed to restore proper anatomy. </p>
<p><strong>Jones Fractures:</strong>  Jones fractures occur in a small section of the bone where there is a poor blood supply feeding the bone.  Any injuries that occur in this “zone” are prone to complications.  Jones fractures are treated both with non-weight bearing casts and surgery.  They can develop a non-union (a broken bone which has given up trying to heal) if not treated by a qualified podiatrist.  These fractures are common in athletes as they general require high energy movements.</p>
<p><strong>Mid-shaft fractures:</strong>  These fractures occur by twisting the forefoot.  Walking off a curb and twisting your foot is a very common way to sustain this type of injury.  A definite snap is felt and sometimes heard.  The foot quickly becomes swollen and bruised.  Treatment usually requires surgery, depending on the amount of damage.  If the bone has not shifted too much, a cast boot can be applied.  However, if there is moderate movement of the broken bones at the time of injury, a plate and screws will be required to restore normal anatomy.</p>
<h3>Outlook:</h3>
<p>Fractures of the fifth metatarsal are quite common, their treatment varies depending on the location of the bone in which the injury occurred.  Long term, if treated appropriately, there is little change of arthritis or continued pain.  Patients are usually able to resume all athletic activities once the area has healed.</p>
 

