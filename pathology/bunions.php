              <div class="tab-pane fade" id="treatment">
                  <h3>Treatment</h3>
                    <p itemprop="possibletreatment">Treatment falls into two broad categories, conservative and surgical. From a conservative standpoint, efforts are directed at correcting faulty foot mechanics with custom molded insoles and relief of symptoms. These include:</p>
                    <ul>
                        <li itemprop="possibletreatment">Custom Orthosis to stabilize the abnormal motion of the hind and fore foot.</li>
                        <li itemprop="possibletreatment">Shoe gear modification:  Using shoes with larger toe boxed and more supple materials.</li>
                        <li itemprop="possibletreatment">Changes in activities:  Try to avoid those things which cause symptoms.</li>
                        <li itemprop="possibletreatment">Anti-inflammatory medication for periodic relief this includes cortisone injections into the joint as well as oral medication.</li>
                    </ul>
              </div>
              
