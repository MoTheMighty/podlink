                <div class="tab-pane fade" id="symptoms">
                   <p itemprop="signorsymptom">The most common symptoms associated with this condition are pain on the side of the foot just behind the great toe. A red painful bump is usually present. Pain is usually brought on with walking or sports. Shoes don't cause bunions but will typically aggravate them. Stiff leather shoes or shoes with a tapered toe box are the prime offenders. This is why bunion pain is most common in women whose shoes have a pointed toe box. The bunion site will often be slightly swollen and red from the constant rubbing and irritation of a shoe. Occasionally, corns can develop between the 1st and 2nd toe from the pressure the toes rubbing against each other.</p>
