                            <div class="tab-pane fade" id="severe">
                                 <h4>Proximal bunion surgery (proximal bunionectomy):</h4>
                                      <p>This is the term used to describe surgery which is performed in the around the big toe joint and in the middle part of the foot. Procedures we perform include Lapidus bunionectomy and crecentic bunionectomy. Indications include:</p>
                                    
                                   
                                      <ul>
                                          <li>Moderate to severe deformity;</li>
                                          <li>Hypermobility (excessive motion) of the metatarsal;</li>
                                          <li>Rapidly progressing deformity.</li>
                                      </ul>
                                      <h4>Details of a proximal bunion surgery:</h4>
                                      <ul>
                                          <li>General anesthesia or local anesthesia</li>
                                          <li>Incisions are located around the great toe joint and the midfoot.</li>
                                          <li itemprop="expectedprognosis">No weight is allowed on the foot until healing of the fusion site is seen on x-ray (usually 6 weeks);</li>
                                          <li itemprop="expectedprognosis">Return to work in 2 to 3 weeks if you primarily work in an office setting, sitting most of the day. If you work on your feet throughout the day, such as in heavy industry (wearing steel toed boots, no light duty) you may require 10 to 12 weeks off of work.</li>
                                          <li itemprop="expectedprognosis">Return to low impact cardio (walking, elliptical trainer) in 8 to 10 weeks.</li>
                                          <li itemprop="expectedprognosis">Return to high impact activities (running, soccer, softball, etc) in 4 to 6 months</li>
                                          <li itemprop="expectedprognosis">Return to athletic shoes in 8 to 10 weeks, other style of shoes will depend on extent of pre-operative joint damage.</li>
                                      </ul>
                                      
                              
