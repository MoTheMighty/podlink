              <div class="tab-pane fade" id="goals">
                                    <h3>Surgical management:</h3>
                      <p>Goals of surgery:</p>
                      <p>The primary goal of bunion surgery is to relieve the pain associated with the deformity. This is accomplished by:</p>
                      <ul>
                          <li itemprop="possibletreatment">correcting the underlying abnormal metatarsal position by realigning it toward the second toe.</li>
                          <li itemprop="possibletreatment">Removing excessive bone formation on the bunion "bump"</li>
                          <li itemprop="possibletreatment">releasing the soft tissue tightness which is pulling the big toe towards the second toe.</li>
                          <li itemprop="possibletreatment">Tightening the soft tissues which are overly stretched on the bump side of the joint.</li>
                          <li itemprop="possibletreatment">Reestablish the correct alignment of the cartilage surfaces.</li>
                          <li itemprop="possibletreatment">Move the sesamoid bones into correct alignment.</li>
                          <li itemprop="possibletreatment">Realign the great toe.</li>
                      </ul>
                      <p>Bunion surgery procedures are based on many factors, including health, age and lifestyle of the patient. However, a critical factor in procedure choice is the grading of the bunion deformity.</p>
                      <p><br />
                      </p>
                      <h4>Bunion deformity grading:</h4>
                      <p>Bunions are grade by the severity into three broad categories:</p>
                      <ul>
                          <li itemprop="stage"><strong>Mild:</strong> Patient's symptoms may include pain and throbbing with activities. There is a slight bump. There is no arthritis of the joint.</li>
                          <li itemprop="stage"><strong>Moderate:</strong> Patient's symptoms may include pain and throbbing most days regardless of activity level. Patient has difficulty wearing most styles of shoes without pain. There is a moderate bump. Some arthritis maybe present in the joint. The big toe is pressing firmly against the second toe.</li>
                          <li itemprop="stage"><strong>Severe:</strong> Patient's has pain daily. Large bump which is red and irritated from shoes and may deform shoes. Inability to wear all styles of closed shoes without pain. Arthritis is present within the joint. The big toe and second toe may overlap. The big toe cannot be straightened manually.</li>
                      </ul>
                      </div>
                
                    
                       
                        <div class="tab-pane fade" id="moderate">
                          <h4>Distal Bunion surgery (Distal Bunionectomy):</h4>
                          
                            <p>This is the term used to describe surgery which is performed exclusively around the great toe joint. There are several proximal procedures we perform, including "Austin" or chevron procedure and "distal L" or Reverdin-Laird procedure. Indication for a proximal surgery include:</p>
                           
                           
                        
                   
                            <ul>
                                <li>Mild to moderate bunion deformity;</li>
                                <li>Inability to tolerate proximal procedure;</li>
                                <li>Minimal to no arthritis within the joint.</li>
                            </ul>
                            <h4 itemprop="stage">Details of distal bunion surgery:</h4>
                
                    
                            <ul>
                                <li>Performed under a local anesthetic. You are, however, sedated by an anesthesiologist prior to injection of the numbing agent to your foot.</li>
                                <li>Incisions are located around the great toe joint only.</li>
                                <li itemprop="expectedprognosis">Weight bearing is started right after surgery unless instructed otherwise.</li>
                                <li itemprop="expectedprognosis">Return to work in 1 to 2 weeks if you primarily work in an office setting, sitting most of the day. If you work on your feet throughout the day, such as in heavy industry (wearing steel toed boots, no light duty) you may require 4 to 6 weeks off of work.</li>
                                <li itemprop="expectedprognosis">Return to low impact cardio (walking, elliptical trainer) in 4-5 weeks.</li>
                                <li itemprop="expectedprognosis">Return to high impact activities (running, soccer, softball, etc) in 10-15 weeks.</li>
                                <li itemprop="expectedprognosis">Return to athletic shoes in 1 month, dress shoes without a heel in 2 months, high heels in 3 months.</li>
                            </ul>
                              
