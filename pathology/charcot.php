                          <p> <span itemprop="cause">Charcot foot can occur in a diabetic who has neuropathy (nerve damage) in the foot that impairs the ability to feel pain. Charot foot typically occurs following a minor injury, such as a sprain or stress fracture</span>. The prevalence of Charcotarthropathy ranges from 0.1% to as high as 13%. In patients with diabetes, the incidence of acute Charcot arthropathyof the foot and ankle ranges from 0.15-2.5%. <span itemprop="epidemiology">Epidemiologic studies do not distinguish between acute and postacute disease. Bilateral disease occurs in less than 10% of patients. Recurrence of disease occurs in less than 5% of patients. Some studies indicate that men and women are equally affected, while others report a 3:1 predilection for males.</span></p> <p itemprop="riskfactor">Because the patient doesn't feel the injury, he or she continues to walk, making the injury worse. Bones fracture, joints collapse and the foot becomes swollen, red and deformed. The patient continues to walk on the deformed foot because they do not feel any pain. The continued abnormal forces on the deformed foot result in serious ulcerations which can require amputation. As the disorder progresses, the joints collapse and the foot takes on an abnormal shape, such as a rocker-bottom appearance.</p>  <p>Charcot foot is a very serious condition that can lead to severe deformity, disability, and even amputation. Because of its seriousness, it is important that patients with diabetes—a disease often associated with neuropathy—take preventive measures and seek immediate care if signs or symptoms appear.  The obesity epidemic is increasing the incidence of Charcot foot.  The excess weight increases the risk of developing diabetes. Once the patients has diabetes, nerve damage to the feet can result. Nerve damage combined with obesity can then lead to micro trauma on the bones which in turn causes Charcot joint to develop. <a href="http://uthscsa.edu/hscnews