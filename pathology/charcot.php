                    <p> The symptoms of Charcot foot may include:</p> 
                  <ul> 
                    <li> <p itemprop="signorsymptom">Warmth to the touch (the affected foot feels warmer than the other)</p> </li> 
                    <li> <p itemprop="signorsymptom">Redness in the foot</p> </li> 
                    <li> <p itemprop="signorsymptom">Swelling in the area</p> </li> 
                    <li> <p itemprop="signorsymptom">Pain or soreness</p> </li> 
                  </ul>
           
                  <p> <span itemprop="possibletreatment">The goal of Charcot foot treatment is to stabilize the joint. In patients with diabetes avoiding placing weight on the foot for at least 8 weeks may help to prevent further damage. During this time, a total contact or walking cast and crutches may be required to protect the foot.</span></p> <p>Traditional surgical techniques, in which bones are held in place by internal plates and screws, don't work with a subset of obese Charcot patients. Their bones, already weakened by complications of Charcot foot, could collapse under the patient's heavy weight.</p> <p>A common podiatric treatment in such cases is to place the patient in a fiberglass cast. But bones can heal in deformed positions. And, it is difficult or impossible for obese patients to walk on one leg when the other leg is in a cast. Patients typically have to use wheelchairs and are confined to the first story of the house for as long as nine months. And after the cast comes off, they must wear a cumbersome leg brace.</p> <p>By contrast, patients who are treated with an external fixator often are able to walk or at least bear some weight on the treated leg. The device is attached to the leg for only two or three months.</p>
             

