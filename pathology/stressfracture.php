                    <h3>Background</h3>
                  </div>
                    <p><img class="pull-right" alt="Stress Fracture" src="/images/pathology/stress-fracture.png" />A stress fracture is one type of incomplete fracture in bones. It is caused by <span itemprop="cause">unusual or repeated stress." This is in contrast to other types of fractures, which are usually characterized by a solitary, severe impact.</span> It could be described as a very small sliver or crack in the bone; this is why it is sometimes dubbed <span itemprop="name">"hairline fracture".</span> It typically occurs in weight-bearing bones, such as the metatarsals(bones of the foot).</p> 
                    <p>It is a common sports injury, and most cases are associated with athletics.</p> 
                    <div class="page-header">
                      <h3>Symptoms</h3>
                    <p>Stress fractures usually have only a few symptoms. A stress fracture could present as a generalized area of pain and tenderness associated with weight bearing. <span itemprop="signorsymptom">Usually when running, a stress fracture in the leg or foot will cause severe pain at the beginning of the run, moderate pain in the middle of the run, and severe pain at the end and after the run.  Additionally, subtle swelling of the affected foot when compared with the other foot can show some</span></p> 
                    <div class="thumbnail center">
                      <img alt="unilateral edema from stress fracture of forefoot" src="/images/pathology/clinicalstressfx.png" class="img-rounded img-responsive" />
                      <p class="text-muted small center">Note subtle swelling or fullness to right foot</p>
                  </div>

                    <div class="page-header">
                    <h3>Cause</h3>
                    </div>
                    <p><span itemprop="cause">Bones are constantly attempting to remodel and repair themselves, especially during a sport where extraordinary stress is applied to the bone. Over time, if enough stress is placed on the bone that it exhausts the capacity of the bone to remodel, a weakened site—a stress fracture—on the bone may appear.</span> <span itemprop="signorsymptom"> The fracture does not appear suddenly. It occurs from repeated traumas, none of which is sufficient to cause a sudden break, but which, when added together, overwhelm the osteoblasts that remodel the bone.</span></p> 
                    <p>Stress fractures commonly occur in sedentary people who suddenly undertake a burst of exercise (whose bones are not used to the task). They may also occur in Olympic-class athletes who do extraordinary quantities of high-impact exercise, in professional and amateur distance runners who run high weekly mileage, or in soldiers who march long distances.</p> 
                    <p><span itemprop="cause">Muscle fatigue can also play a role in the occurrence of stress fractures. Consider a human runner. With each stride, large forces are exerted at various places in the legs.</span> Each shock—a rapid acceleration and energy transfer—must be absorbed. Both muscles and bones serve as shock absorber. However, the muscles, usually those in the lower leg, become fatigued after running a long distance and lose their ability to absorb shock. As the bones now experience larger stresses, this increases the risk of fracture.</p> 
                    <p>Previous stress fractures have been identified as a risk factor.</p>

                    <div class="page-header">
                    <h3>Diagnosis and Treatment</h3>
                    </div> 
                    <p>As with most conditions, a stress fracture is diagnosed with a history and physical exam.</p> 
                    <p>X-rays may not show any evidence of stress fractures, so a CT scan, MRI, or bone scan may be more effective in unclear cases.</p>
