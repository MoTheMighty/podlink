                    <p>If a stress fracture occurs in a weight-bearing bone, <span itemprop="possibletreatment">healing will be delayed or prevented by continuing to put weight on that limb.</span></p> 
                    <p itemprop="possibletreatment">Rest is the only option for complete healing of a stress fracture. The amount of recovery time varies greatly depending upon the location, severity, the strength of the body's healing response and an individual's nutritional intake. Complete rest and a cast or walking boot are usually used for a period of four to eight weeks, although periods of rest of twelve to sixteen weeks is not uncommon for more severe stress fractures. After this period activities may be gradually resumed, as long as the activities do not cause pain. While the bone may feel healed and not hurt during daily activity, the process of bone remodeling may take place for many months after the injury feels healed, and incidences of re-fracturing the bone is still at significant risk. Activities such as running or sports that place additional stress on the bone should only gradually be resumed. One general rule is to not increase the volume of training by more than 10 percent from one week to the next.</p> <p>Rehabilitation usually includes muscle strength training to help dissipate the forces transmitted to the bones.</p> 
                      
                      
                    


                    <p>Bracing or casting the limb with a hard plastic boot or air cast may also prove beneficial by taking some stress off the stress fracture. An air cast has preinflated cells that put light pressure on the bone, which promotes healing by increasing blood flow to the area. This also reduces pain because of the pressure applied to the bone. If the stress fracture of the leg or foot is severe enough, crutches can help by removing stress from the bone.</p> 
                    <div class="thumbnail center">
                      <img alt="fracture boot" src="/images/pathology/fractureboot.jpg" class="img-rounded img-responsive" />
                      <p class="text-muted small center">Typical style boot needed for treatment of stress fractures of the foot and ankle</p>
                    </div>

                    <p>With severe stress fractures, surgery may be needed for proper healing. The procedure may involve pinning the fracture site, and rehabilitation can take up to a half year.</p> 
                    
                    <div class="page-header">
                    <h3>Prevention</h3>
                    </div>

                    <p itemprop="primaryprevention">One method of avoiding stress fractures is to add more stress to the bones. Though this may seem counter-intuitive (because stress fractures are caused by too much stress on the bone), moderate stress applied to the bone in a controlled manner can strengthen the bone and make it less susceptible to a stress fracture. An easy way to do this is to follow the runner's rule of increasing distance by no more than 10 percent per week. This allows the bones to adapt to the added stress so they are able to withstand greater stress in the future.</p> 
                    <p itemprop="primaryprevention"><em><strong>Strengthening exercises</strong></em> also help build muscle strength in the legs. Strengthening these muscles will prevent them from becoming fatigued quickly, allowing them to absorb the strain of running for longer periods of time. Key muscles that need strengthening with lower leg stress fractures are the calves and the shin muscles. Runners often suffer from overuse injuries or repetitive stress injuries. These include stress fractures, stress reactions, tendinitis, meniscal tears, ITB Friction syndrome, and exacerbation of pre-existing arthritis. Stress fractures, if not diagnosed and treated, can develop into complete fractures.</p> <p itemprop="primaryprevention">Depending on a variety of factors (including weight, running surface and shoe durability), runners should replace their shoes every 300–700 miles to allow adequate mid-sole cushioning. A change in running surfaces can also help prevent stress fractures.</p>
