        <h3>Treatment</h3> 
      </div>
      <p>Because it is difficult to treat or eradicate toenail fungus, it is a good idea to try to prevent it. <span itemprop="primaryprevention">It helps to wear protective shoes or sandals in public showers, pool areas and gyms, and to avoid borrowing someone elses shoes or sharing socks or towels with someone who has toenail fungus</span>. An orthotic device can be used to add cushioning and/or control over-pronation, support the longitudinal arch, and reduce stress on the lower leg muscles.</p> <p itemprop="primaryprevention">Wash your feet regularly, and dry them thoroughly when they get wet. Wearing nail polish on the toes is not advised because it can seal in fungus and allow it to grow. Keep toenails trimmed, and be sure to disinfect any pedicure tools before using them.</p> <p>If you do develop toenail fungus, see your foot doctor. The doctor might remove as much of the nail as possible by trimming, filing or dissolving it. Medicated nail polish might be prescribed for a localized infection, but a serious infection will likely be treated with a prescription oral antifungal medication. These medications can have side effects, so be sure to work closely with your doctor on your treatment plan. Only in severe cases will surgical removal of the nail be recommended.</p> <p>If you suspect that you have toenail fungus, see your foot doctor.</p>

