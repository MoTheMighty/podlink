                    <h4>Diagnosis</h4>
                    <p>Radiographs, ultrasound and MRI along with a physical signs and symptoms are utilized to diagnose the condition.</p>
                    <h4>Treatment</h4>
                    <p>Non-surgical treatment of Achilles tendonitis is aimed at resting the area, stretching the tendon and improving range of motion. Non-surgical treatment may include medication, icing, stretching, lifts, orthosis, physical therapy and extra corporeal shockwave treatment.
                    If non-surgical treatment fails to provide adequate pain relief, surgery may be needed. A variety of surgical techniques can be utilized to restore function.</p>
