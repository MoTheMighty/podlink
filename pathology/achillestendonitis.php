              <div class="tab-pane fade" id="signs_symptoms">
                  <p>The pain caused by Achilles tendonitis can develop gradually without a history of trauma. If left untreated, Achilles tendonitis can often lead to rupture. Symptoms include pain during exercise, swelling, redness, pain in the pack of the ankle when arising from rest, inability to walk without limping, or a cracking sensation. Squeezing the tendon from side to side can sometimes elicit significant pain.</p>
