                  <p itemprop="associatedanatomy"><span class="pull-left"> <img src="/images/pathology/achilles.jpg" class="img img-responsive" alt="diagram of achilles tendon"></span>The Achilles tendon is the largest tendon in the body and is found on the back of the ankle.  Its primary function is to help you lift the back of your heel off the ground as you walk or run.  The tendon connects the calf muscles to the back of the heel.  Since we use the tendon with every step, it is subject to repetitive forces.  If these forces are abnormal, a repetitive use injury can develop.</p>
                    <p><strong>What is a repetitive use injury?</strong>  It is a term used to describe an injury which is caused by performing motions over and over.  A familiar example is carpal tunnel injury, which occurs from things such as typing and clicking your mouse.  In the same way, many Achilles tendon injuries are caused by walking on your feet and having ABNORMAL forces placed on the tendon.</p>
                    <p><strong>What are abnormal forces?</strong>  One example would be a person who is overweight.  Each step is placing forces on the Achilles tendon which are in excess of its capacity.  As we abnormally stress the tendon with each step, cumulative damage and degeneration ensues.  At first this damage will not necessarily be noticeable, but as time passes the damage accumulates, symptoms will develop.  Another example is a person who starts an intense exercise program without  properly conditioning the body to work at such stressful levels.</p>
                    <p>The Achilles tendon does not have a good blood supply or the ability to heal itself as effectively as other tendons in out body, so it is more prone to injury from repetitive stresses. Once the injury starts, the body is unable to effectively repair the damage and a chronic degeneration and scarring of the tissue occurs.  If left untreated, the tendon will begin to degenerate and scar tissue will form.  This is known as Achilles tendonosis.   Achilles tendonosis can lead to complete rupture of the Achilles tendon.</p>
