                <h4>Achilles Tendonitis</h4>
                  <p>Achilles tendonitis causes scarring and degeneration of the tendon. The Achilles tendon is the largest tendon in the body. It is located in the back of the leg that inserts into the back of the heel bone. The Achilles tendon is responsible for lifting the heel off the ground when walking or running.  The Achilles tendon does not have a good blood supply or cellular activity, so it is prone to injury from repetitive stresses. Once the injury starts, the body is unable to effectively repair the damage and a chronic degeneration and scarring of the tissue ensues.</p>

