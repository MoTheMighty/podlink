                    <p>There are two types of achilles tendonitis:</p>
                      <div class="ol">
                    <p><li><strong>Midsubstance Achilles tendonitis:</strong>  The "middle" or midsubstance of the Achilles tendon has an inherent lack of good blood circulation which is common to everyone.  When this area becomes damaged, it will often form a painful lump right in the middle of the tendon which can eventually tear or rupture.</li></p>
                    <li><strong>Insertional Achilles tendonitis:</strong>  The area where the achilles tendon attaches to the heel bone can become inflamed and calcifications can form in this area.  Often, these calcifications can grow and a hard painful bump will form on the back of your heel bone.  The area is usually warm and painful to the touch.</li>

