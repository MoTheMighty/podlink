      <div class="page-header">
        <h3>Background</h3>
      </div>

      <p>Haglund’s deformity is a bony enlargement on the back of the heel that most often leads
        to painful bursitis, which is an inflammation of the bursa (a fluid-filled sac between the
        tendon and bone). In Haglund’s deformity, the soft tissue near the Achilles tendon
        becomes irritated when the bony enlargement rubs against shoes.</p> 
        <p>Haglund’s deformity is often called “pump bump” because the rigid backs of pump-style
        shoes can create pressure that aggravates the enlargement when walking. In fact, the
        deformity is most common in young women who wear pumps.</p>

        <div class="page-header">
          <h3>Symptoms</h3>
        </div>
        <p>Haglund’s deformity can occur in one or both feet. The signs and symptoms include:</p> 
        <ul> 
        <li>A noticeable bump on the back of heel.</li> 
        <li> Pain in the area where the Achilles tendon attaches to the heel.</li> 
        <li>Swelling in the back of the heel.</li> 
        <li>Redness near the inflamed tissue.</li> 
        </ul> 
        <div class="page-header">
          <h3>Cause</h3>
        </div>
        <p>To some extent, heredity plays a role in Haglund’s deformity. People can inherit a type
        of foot structure that makes them prone to developing this condition.</p> 
        <p>For example, high arches can contribute to Haglund’s deformity. The Achilles tendon
        attaches to the back of the heel bone, and in a person with high arches, the heel bone is
        tilted backward into the Achilles tendon. This causes the uppermost portion of the back
        of the heel bone to rub against the tendon. Eventually, due to this constant irritation, a
        bony protrusion develops and the bursa becomes inflamed. It is the inflamed bursa that
        produces the redness and swelling associated with Haglund’s deformity.</p> 
        <p>A tight Achilles tendon can also play a role in Haglund’s deformity, causing pain by
        compressing the tender and inflamed bursa. In contrast, a tendon that is more flexible
        results in less pressure against the painful bursa.</p> 
        <p>Another possible contributor to Haglund’s deformity is a tendency to walk on the outside
        of the heel. This tendency, which produces wear on the outer edge of the sole of the
        shoe, causes the heel to rotate inward, resulting in a grinding of the heel bone against
        the tendon. The tendon protects itself by forming a bursa, which eventually becomes
        inflamed and tender.</p> 

        <div class="page-header">
          <h3>Diagnosis and Treatment</h3>
        </div>
        <p>Radiographs, ultrasound and MRI along with a physical signs and symptoms are utilized to diagnose the condition.</p>

    

        <p>Non-surgical treatment of Haglund’s deformity is aimed at reducing the inflammation of
          the bursa. While these approaches can resolve the bursitis, they will not shrink the bony
          protrusion. Non-surgical treatment can include medication, icing, stretching, lifts,
          orthosis, physical therapy and extra corporeal.
          If non-surgical treatment fails to provide adequate pain relief, surgery may be needed.</p> <p>A variety of surgical techniques can be utilized to restore function</p>
