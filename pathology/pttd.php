                  <div class="page-header">
                    <h3>Cause</h3>
                  </div>

                  <p itemprop="associatedanatomy">Posterior tibial tendonitis is an inflammation of the posterior tibial tendon and its sheath. It extends from the inside of the ankle to the arch of the foot.</p> 
                  <p>Tendons are fibrous cords that connect muscles to bones. The posterior tibialis tendon helps maintain the arch of the foot and is used to turn the foot in (the medical term for this is “inversion”) and to help flex it (plantar flexion). Injury to this tendon can result in loss of function, primarily affecting push off from the ground with walking and running.</p> 
                   
                  <ul> 
                  <li itemprop="cause">Too much running, dancing, or other athletic activity</li> 
                  <li itemprop="cause">Trauma with high impact</li> 
                  <li itemprop="cause"><strong>Pronation</strong>, which is walking on the inner aspect of the foot. Walking this way alters the normal forces in the feet—the arch flattens and the foot turns outward. To compensate, the muscles work harder and become tired. This may lead to chronic inflammation of the tendon. Long term, it can cause the tendon to deteriorate, lengthen, and even completely rupture. If that happens, the foot flattens. This condition is called flatfoot deformity.</li> 
                  <li itemprop="cause"><strong>Aging.</strong> With age, the posterior tibial muscle and tendon may become weaker and cause flattening of the arch. This puts further stress on the tendon. Other foot conditions may contribute to loss of the arch over time.</li> 
                  <li itemprop="cause"><strong>Chronic inflammation</strong>, which is associated with rheumatoid arthritis or other arthritic conditions.</li> 
                  </ul> 
                  <p><strong>Risk Factors</strong></p> 
                  <ul> 
                  <li>A risk factor is something that increases your chance of getting a disease or condition. Risk factors for posterior tibial tendonitis include:</li> 
                  <li itemprop="riskfactor">History of rheumatoid arthritis or other arthritic conditions</li> 
                  <li itemprop="riskfactor">Running and jumping</li> 
                  <li itemprop="riskfactor">Dancing, for example ballet pointe work and excessive foot rotation</li> 
                  <li itemprop="riskfactor">Pronated feet</li> 
                  </ul>
                
                  <div class="page-header">
                    <h3>Signs and Symptoms</h3>
                  </div>
                <ul> 
                <li itemprop="signorsymptom">Pain and swelling near the arch of the foot and on the medial side of the ankle</li> 
                <li itemprop="signorsymptom">Pain that increases when standing on the ball of the foot or if the foot is flexed</li> 
                <li itemprop="signorsymptom">Pain that increases with activity</li> 
                <li itemprop="signorsymptom">Tiredness in the foot after little activity</li> 
                <li itemprop="signorsymptom">Pain that becomes more disabling</li> <li>Later in the course of the disease, a flattening of the arch of the foot</li> 
                <li itemprop="signorsymptom">An inability to push off well when running</li> 
                </ul> 

                <div class="page-header">
                    <h3>Diagnosis</h3>
                  </div>
                The podiatrist will ask about your symptoms and medical history, and perform a physical exam, paying special attention to the foot. Posterior tibialis tendonitis can be difficult to diagnose because pain in the ankle and foot can be due to many causes.</p> <p>The doctor will try to feel the tendon through your skin and note how the foot moves and handles resistance to moving the foot inwards. He or she will look at the foot from behind and from the side and see how flat the arch is. The podiatrist may also ask you to try to stand on the ball of your foot. If you cannot do this you are likely to have a problem with your posterior tibial tendon.</p> 
                <p><strong>Tests may include:</strong></p> 
                <ul> <li itemprop="typicaltest">X-rays —a test that uses radiation to take a picture of structures inside the body, in this case the foot and leg</li> 
                <li itemprop="typicaltest">MRI scan —a test that uses magnetic waves to make pictures of the inside of the body, in this case looking at the posterior tibial tendon</li> 
                </ul>
                 

                 <div class="page-header">
                    <h3>Treatment</h3>
                  </div>
                <p>Goals of treatment:</p> 
                <ul> 
                <li>Prevent the foot from pronating</li> 
                <li>Decrease inflammation and prevent deterioration of the tendon</li> 
                </ul> 
                <p>You may be referred to an podiatrist. Early treatment and correction of the underlying cause improves the prognosis.</p>
                <p>Treatment may include:</p> 
                <ul> 
                <li itemprop="possibletreatment">Rest</li> 
                <li itemprop="possibletreatment">Strapping or taping the foot; some people need a cast or a brace</li> 
                <li itemprop="possibletreatment">Nonsteroidal anti-inflammatory drugs (NSAIDs)</li> 
                <li itemprop="possibletreatment">Cortisone injection into the tendon sheath, although multiple injections may further damage the tendon</li> 
                <li>Physical therapy, including:</li> 
                <li itemprop="possibletreatment">Exercises to strengthen the posterior tibial muscle</li> 
                <li itemprop="possibletreatment">Correction of poor dance or exercise techniques</li> 
                <li itemprop="possibletreatment">Custom-made orthotics—These are shoe inserts that can decrease tension on the tendon, reduce pronation, and help support the foot.</li> 
                <li>Surgery to repair the tendon</li> 
                </ul> 
                <div class="page-header">
                    <h3>Prevention</h3>
                  </div>
                <p>To help prevent posterior tibialis tendonitis, practice good foot care:</p> 
                <ul> 
                <li>If you notice you are walking on the inner aspect of your foot, consult a doctor.</li> 
                <li>If you have a foot or ankle injury, get medical care.</li> 
                <li>Wear good, supportive shoes that provide arch support.</li> 
                <li>Obtain proper training for sports and dance activities.</li> 
                </ul>
            

