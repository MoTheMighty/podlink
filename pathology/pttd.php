                <p><strong>Symptoms may include:</strong></p> <ul> <li>Pain and swelling near the arch of the foot and on the medial side of the ankle</li> <li>Pain that increases when standing on the ball of the foot or if the foot is flexed</li> <li>Pain that increases with activity</li> <li>Tiredness in the foot after little activity</li> <li>Pain that becomes more disabling</li> <li>Later in the course of the disease, a flattening of the arch of the foot</li> <li>An inability to push off well when running</li> </ul> <p><strong>Diagnosis</strong><br />
                The doctor will ask about your symptoms and medical history, and perform a physical exam, paying special attention to the foot. Posterior tibialis tendonitis can be difficult to diagnose because pain in the ankle and foot can be due to many causes.</p> <p>The doctor will try to feel the tendon through your skin and note how the foot moves and handles resistance to moving the foot inwards. He or she will look at the foot from behind and from the side and see how flat the arch is. The doctor may also ask you to try to stand on the ball of your foot. If you cannot do this you are likely to have a problem with your posterior tibial tendon.</p> <p><strong>Tests may include:</strong></p> <ul> <li>X-rays —a test that uses radiation to take a picture of structures inside the body, in this case the foot and leg</li> <li>MRI scan —a test that uses magnetic waves to make pictures of the inside of the body, in this case looking at the posterior tibial tendon</li> </ul>
