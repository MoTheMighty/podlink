                  <p>Posterior tibial tendonitis is an inflammation of the posterior tibial tendon and its sheath. It affects this tendon as it runs from the inside of the ankle to the arch of the foot.</p> <p>Tendons are fibrous cords that connect muscles to bones. The posterior tibialis tendon helps maintain the arch of the foot and is used to turn the foot in (the medical term for this is “inversion”) and to help flex it (plantar flexion). Injury to this tendon can result in loss of function, primarily affecting push off from the ground with walking and running.</p> <p><strong>Causes</strong></p> <ul> <li>Too much running, dancing, or other athletic activity</li> <li>Trauma with high impact</li> <li><strong>Pronation</strong>, which is walking on the inner aspect of the foot. Walking this way alters the normal forces in the feet—the arch flattens and the foot turns outward. To compensate, the muscles work harder and become tired. This may lead to chronic inflammation of the tendon. Long term, it can cause the tendon to deteriorate, lengthen, and even completely rupture. If that happens, the foot flattens. This condition is called flatfoot deformity.</li> <li><strong>Aging.</strong> With age, the posterior tibial muscle and tendon may become weaker and cause flattening of the arch. This puts further stress on the tendon. Other foot conditions may contribute to loss of the arch over time.</li> <li><strong>Chronic inflammation</strong>, which is associated with rheumatoid arthritis or other arthritic conditions.</li> </ul> <p><strong>Risk Factors</strong></p> <ul> <li>A risk factor is something that increases your chance of getting a disease or condition. Risk factors for posterior tibial tendonitis include:</li> <li>History of rheumatoid arthritis or other arthritic conditions</li> <li>Running and jumping</li> <li>Dancing, for example ballet pointe work and excessive foot rotation</li> <li>Pronated feet</li> </ul>
