                <p><strong>Treatment</strong><br />
                The goals of treatment are to:</p> <ul> <li>Prevent the foot from pronating</li> <li>Decrease inflammation and prevent deterioration of the tendon</li> </ul> <p>You may be referred to an orthopedic specialist. Early treatment and correction of the underlying cause improves the prognosis.<br />
                Treatment may include:</p> <ul> <li>Rest</li> <li>Strapping or taping the foot; some people need a cast or a brace</li> <li>Nonsteroidal anti-inflammatory drugs (NSAIDs)</li> <li>Cortisone injection into the tendon sheath, although multiple injections may further damage the tendon</li> <li>Physical therapy, including:</li> <li>Exercises to strengthen the posterior tibial muscle</li> <li>Correction of poor dance or exercise techniques</li> <li>Custom-made orthotics—These are shoe inserts that can decrease tension on the tendon, reduce pronation, and help support the foot.</li> <li>Surgery to repair the tendon</li> </ul> <p><strong>Prevention</strong><br />
                To help prevent posterior tibialis tendonitis, practice good foot care:</p> <ul> <li>If you notice you are walking on the inner aspect of your foot, consult a doctor.</li> <li>If you have a foot or ankle injury, get medical care.</li> <li>Wear good, supportive shoes that provide arch support.</li> <li>Obtain proper training for sports and dance activities.</li> </ul>
