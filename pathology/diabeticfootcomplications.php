                        <h5>How can I help prevent diabetic foot complications?</h5> <p>Check your feet every day and follow the foot care guidelines given below.</p> <p>Look at your feet with a mirror at the end of each day to make sure you have no reddened areas,
                      cuts, or scrapes that could become infected.</p> <p>Bathing and drying:</p> 
                      <ul> 
                      <li>Wash your feet every day with soap and warm (not hot) water. Then dry your feet
                          carefully, especially between the toes. Apply lanolin after your feet are dry to keep the
                          skin soft and free of scales.</li> 
                          <li>If your feet sweat a lot, keep them dry by dusting them with talcum powder.</li> 
                          </ul> 
                          <p>Treating corns and calluses:</p> 
                          <ul> 
                          <li>Don't treat corns or calluses yourself. Do not use over- the-counter products for these
                          problems unless your health care provider says it is OK.</li> 
                          <li>Tell your health care provider right away if you develop a corn or callus.</li> </ul> <p>Toenail care:</p> <ul> <li>Cut your toenails carefully. Cut them straight across. Do not cut the sides or the cuticles.</li> 
                          <li>Clean your nails carefully.</li> 
                          <li>If your nails are hard to trim, ask your health care provider's office for help.</li> 
                          </ul> 
                          <p>Foot warmth:</p> 
                          <ul> 
                          <li>Wear cotton socks to bed if you need extra warmth for your feet.</li> 
                          <li>Avoid using hot water bottles or electric heaters to warm your feet. Because you may not
                          fully sense hot and cold with your feet, you may burn your feet accidentally and develop
                          an infection.</li> 
                          <li>Avoid putting your feet where they could accidentally be burned; for example, on hot
                          sand at the beach, in hot bath water or whirlpools, or near a fireplace.</li> 
                          </ul> 
                          <p>Footwear:</p> 
                          <ul> 
                          <li>Wear soft leather shoes that fit properly.</li>
                           <li>Ask your health care provider about specially made shoes, especially if you have foot
                          problems.</li> 
                          <li>Avoid wearing new shoes for more than an hour a day until they are thoroughly broken
                          in.</li> 
                          <li>Avoid sitting with your legs crossed.</li> 
                          <li>Wear clean socks and change them at least once a day.</li> 
                          </ul> 
                          <p>In addition to these foot care guidelines, good control of your blood sugar and your blood
                          pressure help prevent foot problems.</p>
