                  <h5>What are diabetic foot complications?</h5> <p>
                        Foot ulcers (sores) and infections are a problem that can be caused by diabetes. The severity of the problem can range from minor sores to serious damage of tissues in the foot.</p> <h5>How do they occur?</h5> <p>When you have diabetes, the circulation of blood to your feet is not as good as it should be. It is harder for the body to fight infections and heal itself. As a result, any infections and sores on your feet can become serious. Without treatment, severe infections can cause the flesh of your foot to die (gangrene). Diabetics are 20 times more likely to have gangrene in the foot than people who do not have diabetes.</p> <p>Because diabetes damages nerve endings, you may not feel pain if you hurt your foot or get an infection. This can make it difficult for you to know that you need medical treatment.</p> <h5>What are the symptoms?</h5> <p>The initial symptoms are swelling, redness, or pain. Sores may appear on the sole of your foot. They may heal but later return in the same place. If the sores are not treated, that part of the flesh of your foot may die and turn black.</p> <h5>How are they treated?</h5> <p>You may need antibiotics or other medications to put on the sore or infected part of your foot. The medicine will help fight infection, rid the wound of dead flesh, and help new, healthy flesh to grow.</p> <p>You may have to stay off your feet for a while to prevent further irritation of the sores or infections.</p> <p>You may be hospitalized for treatment. If antibiotics don't heal the infected or ulcerated area, the
                        doctor may have to remove the infected flesh surgically. If you have gangrene, the doctor may
                        have to amputate part or all of your foot.</p>
