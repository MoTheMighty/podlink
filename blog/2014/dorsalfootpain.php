       <h1>5 causes of top of foot pain</h1>
      <p class="lead">by <a href="../../doctors/perez.php">Dr. Richard Perez</a></p>
      <hr>
      <p><span class="glyphicon glyphicon-time"></span> Posted on January 25, 2014 at 10:40 AM</p>

      <hr>
      <p>

      

        <h4>Background</h4>
        <p itemprop="associatedanatomy">Before talking about causes of pain to the top of the foot, lets review the anatomy.  The top of the foot consists of bones (talus, navicular, cuneiforms, cuboid, metatarsals), several small arteries, small nerves, and tendons.  So any pain generally is a problem with one or a combination of those anatomic structures.</p>
      <img src="../../../blog/images/dorsalfootanatomy.jpg" class="img-responsive">
      <h4 itemprop="name"> 1. Stress Fracture</h4>
      <p itemprop="cause">Stress fractures the the most common cause of top of foot pain.  Usually, these injuries start of as a mild discomfort which gradually progress and become worse.  Swelling will usually occur towards the end of the day or after prolonged activities, only to feel better in the morning.  Stress fracture are caused from repetitive  stress to the bones which results in microscopic injuries.  The body is unable to heal the injuries fast enough and an accumulation of micro fractures leads to increasing pain and swelling.  Eventually, a large crack may form, but not always.  Stress fractures can be a sign of underlying bone disease such as osteoporosis.</p>
      <img src="/images/pathology/clinicalstressfx.png" class="img-responsive">
      <h4 itemprop="name">2. Neuritis</h4>
      <p itemprop="cause">There a several small cutaneous nerves on the top of the foot which can become injured.  These injuries can result from overly tight shoes, high arches or direct blows to the top of the foot.  Symptoms include burning and tingling which can radiate down to the toes.  Usually having the bed covers push down on the foot can cause pain.  Running sporting activities can also make the problem worse.  Swelling is not usually present.</p>

      <h4 itemprop="name">3. Tendonitis</h4>
      <p itemprop="cause">The tendons on the top of the foot are a frequent source of pain.  Especially if you just started a new athletic activity or significantly increased your volume of athletics.  Pain is usually present when you flex your foot upwards and going down stairs.  The can sometimes be mild swelling.  Point your foot downwards also can be painful. Swelling is not usually present. </p>

      <h4 itemprop="name">4. Arthritis</h4>
      <p itemprop="cause"> We don't normally think about having joints on the top of our foot.  But there are many and they frequently suffer from some form of arthritis as we age.  The most common area of arthritis is the mid-foot in a joint called the tarso-metatarsal joint, or "Lis Franc's joint."  Symptoms usually include, pain, redness and swelling in a defined area across the top of the mid-foot.  The symptoms are aggravated with activities such as walking and running.  Sometimes the symptoms may become quite severe, only to disappear for months at a time.</p>

      <h4 itemprop="name">5. Sprain</h4>
      <p>Sprains are not just the privilege of the ankle.  Foot sprains will often affect the top of the foot.  These can be quite serious and lead to long term problems if not treated appropriately.  The sprains usually occur from sports injuries, motor vehicle accidents and falls.  Symptoms include pain and swelling to the top of the foot which is worse when you try and put weight on the foot.  It is important to be evaluated by a specialist as soon as possible to minimize permanently damage.</p>
