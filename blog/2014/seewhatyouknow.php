      <h1>You See What You Know</h1>
      <p class="lead">by <a href="../../doctors/perez.php">Dr. Richard Perez</a></p>
      <hr>
      <p><span class="glyphicon glyphicon-time"></span> Posted on April 6, 2014 at 7:52 PM</p>
      <hr>
      <img src="../../../blog/images/granuloma.jpg" class="img-responsive">
      <hr>
      
    
          <p>I can recall as a fourth year medical student examining a patient who came in complaining of a rash to her foot.  Looking down at her foot, I saw a raised red round rash.</p>
          <p>"You have an athlete’s foot infection" I said.</p>
          <p>Upon presenting the case to my attending, I was confident in my diagnosis.  It was a clear-cut, easy dermatology problem.  Well not so fast.</p>
          <p>You see what you know. </p> 
          <p>This maxim was an early part of my medical education.  And in this case, I knew about athletes foot infections.  Upon entering the exam room, my attending asked the patient if the rash itched to which she replied, no.
          "Granuloma Annulare is the diagnosis student doctor Perez," said my attending.</p>
          <p>You see what you know, and I did not know about this problem.  But that simple experience was an example of the years of study, which are required to become a Podiatrist.</p>
          <p>Granuloma Annulare is not to common and I do not see it often, but when a case came into the office recently, it brought back my previous learning experience with the disease.  It is a benign self-limiting condition, which generally resolves within several weeks without treatment.  </p>
          <p>There is no known cause, however, minor scrapes and medications maybe to blame.  Solitary lesions are common to the foot and generally do not cause any itchiness or pain.  Sizes vary, but generally no larger than a few inches.  It affects both men and women equally and is usually occurs before the age of 30.</p>
          <p>Treatment can consist of topical corticosteroids, however, treatment is usually not needed.</p>
          <p>17 years of practicing podiatric medicine in San Antonio has vastly increased my “see what you know” knowledge base and I am grateful for the opportunity to be a life long learner in this great community.</p>



