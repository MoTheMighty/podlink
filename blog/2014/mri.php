      <p>Since 2012, we are proud to be the first and only (as of this writing) podiatrist in the San Antonio area to offer this advanced imaging modality for our patients.  MRI is a critical part of establishing accurate and timely diagnosis of muscular skeletal conditions.</p>
      <h3>What is an MRI?</h3>
      <p>MRI, stands for Magnetic Resonance Imaging.  So, the <strong>magnetic</strong> part of the name implies that some form of magnetic component is involved.  And, in fact, an MRI machine uses strong magnets to change our bodies magnetic alignment.</p>  <p>Magnetic alignment?</p>  <p>Well, each of our cells has a slight "charge" or a positive pole and negative pole, similar to a double AA battery.  The magnet, in the case of our machine it is a naturally occurring substance called molybdenum which act as a super strong magnet, will literally realign your cell's positive and negative poles (sounds painful, but there is no sensation when this occurs).  As soon as the magnet turns off, the poles go back to their former alignment.  As they move back, they release radio waves, also known as <strong>resonance</strong>, which are picked up by a super sensitive detector, this in turn sends the information to a computer for processing.  The computer then takes all of the information and creates a three dimensional <strong>image</strong> of the inside of our body.</p>
      <h3>What things can you detect with an MRI study?</h3>
      <p>At Alamo Family Foot & Ankle Care we primarily use the MRI to help diagnose ongoing problems which have not responded to typical treatments.  These problems include, stress fractures, tendon tears, arthritis, infection, cartilage damage, tumors, joint dislocations and many more.</p>  <p>By having the MRI readily available in our office we are able to provide patients with timely and accurate information regarding their foot and ankle problems.  All of our MRIs are reviewed by a board certified radiologist with specialization in muscular skeletal MRIs.</p>

