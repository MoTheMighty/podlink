      <h1>Rickets- Victorian era malady with modern day persistence</h1>
      <p class="lead">by <a href="../../doctors/perez.php">Dr. Richard Perez</a></p>
      <hr>
      <p><span class="glyphicon glyphicon-time"></span> Posted on January 10, 2014 at 3:31 PM</p>
      <hr>
      <img src="../../../blog/images/osteomalacia.jpg" class="img-responsive">
      <hr>
      
      <p itemprop="cause">It is not often that I see a Victorian era disease come through the office.  But that's exactly what occurred recently.  Rickets , also known as Osteomalacia  is a bone disease caused primarily by a lack of Vitamin D.</p>
      <p>If you are a baby boomer, think back to sitting at the kitchen table with a box of cereal and milk.  Often times, printed on the side of the carton would be the words  "Fortified with Vitamins and Minerals."  Well, following the discovery of the link between vitamin D and rickets in the 1920's, a public health effort was started to eliminate the disease by adding vitamin D to milk and encouraging mothers to give their children cod liver oil (which was naturally high in vitamin D).  Rickets was largely eliminated from developed countries in the 20th century thanks to these efforts.  The disease, does persist, primarily in  under developed countries, however, the <a href="http://www.telegraph.co.uk/health/healthnews/7043698/Rickets-makes-comeback-among-computer-generation.html">  UK has seen a recent increase in cases</a> secondary to malnutrition and lack of outdoor activities.</p>
      <p itemprop="possibletreatment">Treatment for rickets wasn't always quite so pedestrian.  Prevalence of the disease was high in Europe and New England in the 17th and 18th century.  Treatments of the time included, snail and earthworm "pottage" or stew, sulfuric acid in syrup of roses and laxatives.  In the mid 19th century,  the link between sunlight (which helps in Vitamin D synthesis) and cod liver oil came into focus as a possible treatment for the ailment.</p>  
      <p>Although, I have read about rickets, I had never actually seen a case until very recently.  This particular person, growing up during the depression of the 1930s,  developed rickets as a child, and for reasons he does not recall, the condition was allowed to progress until permanent deformity resulted.  Today, he lives an active life, but is constantly reminded of his childhood disease each time he takes a step.</p>

