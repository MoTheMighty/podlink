              <tr>
                <td>Exercise </td>
                <td>Microscopic trauma to nails from sports such as running, football and soccer cause opportunities for fungus to get under the nail and cause infection</td>
                <td>Insure you are using shoes that are sport specific and are correctly sized.  Shoes that are too small or big can increase the change of trauma to the nail.</td>
              </tr>
               <tr>
                  <td>Cuticles</td>
                  <td>Pushing your cuticles back</td>
                  <td>Cuticles serve a vital function sealing the top of the nail to the skin.  If this "seal" is damaged, fungus will have an easy time getting under the skin and causing infection.</td>
              </tr>
              <tr>
                <td>Work Boots</td>
                <td>being inside a hot work boot all day long causes the foot to sweat, a lot.  The materials in the boot will become damp.  The damp, dark boot promotes fungus.</td>
                <td>Try to allow your boots to dry out by using 2 pairs and alternating the days they are used.</td>
              </tr>
              <tr>
                <td>Communal Showers</td>
                <td>Gyms showers</td>
                <td>Wear flip flops when taking a shower.  Then immediately apply an anti-fungal spray to the feet and between the toes right after your shower, before putting on your shoes.</td>
              </tr>
              <tr>
                <td>Pedicures</td>
                <td>Nail salons</td>
                <td>Make sure your salon is properly sterilizing the instruments between each use.  They should open the instruments from the sterile packs in front of you.  Consider skipping whirlpool spa.</td>
              </tr>
                
              <tr>
                <td>Chronic Diseases</td>
                <td>Diabetes, HIV, Lupus..</td>
                <td>Diseases which lower your immune system make it easier for fungus to multiply once under your skin.</td>
              </tr>

