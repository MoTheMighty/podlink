       <h1>My toenails are gross!</h1>
      <p class="lead">by <a href="../../doctors/perez.php">Dr. Richard Perez</a></p>
      <hr>
      <p><span class="glyphicon glyphicon-time"></span> Posted on May 30, 2014 at 10:47 AM</p>



      <hr>
      

      <p>I often hear words similar to this from many of my patients who seek podiatric care for their toe nail problems.  More often then not, the problem can be blamed on fungus.  Toenail fungus is a very common problem seen by podiatrist.  It is often ignored, starting out as a small discoloration on the tip of the nail.  But with time, that small area grows and grows.  And before you know it, your nails are thick, crumbly yellowish black and you have what seems like a case of really bad dry red skin to the soles of your feet.  Well, you have full blown nail fungus and associated athletes foot infection and a visit to the podiatrist's office is not far behind.</p>

      <p>As, the disease progresses, your nails can even start hurting from pressing against the tops of your shoes.  Yikes!</p>
      <img src="../../../blog/images/fungalToenail.jpg" class="img-responsive center">
    <h4>Prevention</h4>
    <div class="table-responsive">
