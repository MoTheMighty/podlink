          <h4>Home Treatments</h4>
            <p>The best podiatric treatment is prevention!  But that is not useful if you already have nail fungus.</p>
            <p>Things NOT to do</p>
              <ul>
                <li>Soak your foot in vinegar</li>
                <li>Apply bleach to your nails</li>
                <li>Spray Lysol on your nails</li>
                <li>Apply beta-dine to your nails</li>
              </ul>
            <p>Possible treatments:  Soak your feet in warm water for ten minutes, then, using an Emory board, sand down the thickness of your nails as much as possible.</p>
              <ul>
                <li>Apply over the counter anti-fungal nail solution as directed, or;</li>
                <li>Apply Tea tree oil daily to the affected nails, or;</li>
                <li>Apply Vick's Vapor Rub daily to the affected nails.</li>
              </ul>

            <p>Unfortunately, the chance that these treatments will provide any meaningful improvement is less the 10%! </p> <p>Fungus is a very hard problem to treat and usually requires a comprehensive approach, addressing the environment of the feet, the fungal load of the shoes and the active infection.</p>  <p>During my podiatric consultation with patients, I discuss the multi faceted steps which will be required to effectively treat this stubborn problem!</p>
