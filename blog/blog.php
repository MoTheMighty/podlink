      <h1>The Bunion Blues</h1>
      <p class="lead">by <a href="../doctors/mechell.php">Dr. R. Jordan Mechell</a></p>
      <hr>
      <p><span class="glyphicon glyphicon-time"></span> Posted on December 15, 2013 at 7:13 PM</p>

      <hr>
      <p>

        Got the bunion blues.  Don’t worry; you’re not alone.   Let me help shed some light on this matter.  

        <h4>What exactly is a bunion?</h4>
        <p>A bunion is a bump on the side of the foot. A bunion is NOT an extra piece of bone; is NOT an enlargement of bone; is NOT a bone spur and more importantly it is NOT a tumor but it represents progressive disorder caused by a splaying of 2 bones that are normally parallel.  It has been reported that there is a genetic component that plays a role in the development of a bunion along with shoe gear.</p>  
        <p>The front half of our foot contains 5 metatarsal bones (the long bones in the foot) like the metacarpals in our hands.  As the 1st and 2nd metatarsal start to deviate from one another, we start to see the bunion develop.  As the big toe joint becomes more deviated, the side of the 1st metatarsal head becomes more prominent (i.e. the bunion deformity).</p>  <p>As the big toe starts to drift towards the lesser toes, the 1st metatarsal head becomes more prominent.  This causes an uncovering (or exposing) of the cartilage at the 1st metatarsal head.  Cartilage if not utilized begins to wear away causing arthritis to develop in the big toe joint and also in cases where there are two bones rubbing together.</p>  <p>A bunion will also cause the foot to shift the body weight from the great toe joint to the lesser toe joints causing a dysfunction in normal foot biomechanics.  As a bunion develops a dysfunction in normal biomechanics starts to occur. With normal foot mechanics, about 60% of the body weight is carried through the big toe joint.</p>  <p>A bunion causes the great toe joint to become inefficient shifting more of the weight to the other metatarsal bones.  This increases the risk for the foot developing other problems such as stress fractures, hammer-toes, neuromas (swelling of the nerve), or even stress fractures in your lesser metatarsals.  Moreover, it can also cause knee pain as one starts to compensate for pain and lack of motion in the big toe joint.  
      </p>
      <img src="../../../blog/images/bunion.png" class="img-responsive">
      <h4>I have a bunion, now what?</h4>
      <p>Let me first preface this by stating that not all bunions require treatment.  When we evaluate a patient with a bunion, we perform a thorough clinical and radiographical examination to determine the severity of the problem.  Also the patient’s activity level, exercise routine, and basic lifestyle will help determine what level of treatment is required.</p>  <p>Again, bunions are a progressive disorder, so without proper treatment they tend to get worse with time, not better.  
      Some of the conservative treatment options for bunions include bunion pads, toe splints, wider shoes, and orthotics.  Orthotics is a device that is meant to position the foot in a normal, anatomic biomechanical position to help prevent progression of the bunion.</p>  <p>These non-surgical options are not meant to reverse the bunion, they are intended to accommodate for it.  Oftentimes conservative (nonsurgical) treatment can alleviate the pain and discomfort bunions cause.</p>
      <img src="../../../blog/images/bunionsplint.jpg" class="img-responsive">
      <p>Once conservative therapy has failed, or if it has been determined that the severity of the bunion will not respond to non-surgical treatment options, we will consider your surgical options.</p>   <p>There are about 100 different documented surgical procedures to correct a bunion and luckily for you I will spare you the boring details.  The reason for this is that not all bunions are the same, and they usually require different levels of treatment.  After a thorough surgical consultation, we will individualize a surgical plan tailored to you.</p>
      <img src="../../../blog/images/buniondrawing.jpg" class="img-responsive">
      <h4>How Long Is The Recovery After Having Bunion Surgery?</h4>
      <p>So if you are reading this, you may be thinking “This is ME!” or “I have a painful bunion and want it corrected, but I am worried about the recovery time.”  This is common.  Let me first point out that every patient has a different rate of healing.  This is based on several factors: age, medical history (Diabetes, history of radiation therapy, nutritional deficiencies, etc), and social history (smoking).</p>  <p>The most common post-op recovery after a simple bunion surgery is 3 weeks in a post-operative shoe and then transitioning into regular gym shoes.  However, if a more complex surgery is required and performed the post-operative course can include you to be non-weight bearing to the involved foot for up to 6-weeks.   After 6 weeks, the patient’s typically will begin to transition into the regular gym shoes.</p>
