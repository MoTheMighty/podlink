       <h1>Carpentry and bunion surgery</h1>
      <p class="lead">by <a href="../../doctors/perez.php">Dr. Richard Perez</a></p>
      <hr>
      <p><span class="glyphicon glyphicon-time"></span> Posted on November 8, 2015 at 7:13 PM</p>


        <hr>
      <p>There are dozens of different types of bunion surgery.  Many named after the doctor who first published the particular technique.  Scarf bunionectomy, however, is named after a wood working technique referring to a joint made by notching, grooving, or otherwise cutting the ends of two pieces and fastening them together so that they overlap and join firmly into one continuous piece.  It is this concept that drew me towards this performing this type of bunionectomy.</p>
      <p>I have performed many different types and bunion surgery over the years and found pluses and minuses in all of them.  One common theme was stability and returning the patient to normal footwear.  Each procedure had differing times when a patient could return to normal shoe gear.  I wanted to use a technique that would allow for appropriate correction of the bunion and have enough stability to return to shoes in a timely fashion.  That’s where the carpentry roots of the scarf appealed to me.  The type of “cut” made in the bone was inherently stable and thanks to pioneers in our field, had a large volume of scientific papers supporting the stability concept.</p>
      <img src="../../images/scarf.jpg">
      <p>While I had performed the procedure in the past, I wanted to refine my technique.  So I travelled to Chicago to learn from the foremost experts in scarf bunionectomies, with a multitude of published studies on the topic, The Weil Foot & Ankle Institute.  There, I learned several important concepts to avoid common pitfalls of the procedure.  Armed with this knowledge, I have made the scarf bunionectomy my primary bunion correction surgery of choice.  The benefits of the scarf are:</p>
      <ul>
      <li>Inherently stable</li>
      <li>Able to perform both feet at the same time</li>
      <li>Return to athletic shoes within 1 – 3 weeks in most cases</li>
      <li>Able to address mild, moderate and severe bunions with the same technique</li>
      <li>Large volume of scientific literature supporting its attributes</li>
      </ul>
      <p>If you are suffering from a painful bunion, I am happy to discuss if a scarf bunion surgery is right for you.  I am available for pre-operative consultations at our Alamo Heights, Medical Center and Stone Oak offices.</p>



